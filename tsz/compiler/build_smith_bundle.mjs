import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const compilerRoot = path.dirname(fileURLToPath(import.meta.url));
const manifestPath = path.resolve(compilerRoot, 'smith_LOAD_ORDER.txt');
const outPath = path.resolve(compilerRoot, 'dist', 'smith.bundle.js');

function parseManifest(text) {
  return text
    .split('\n')
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith('#'));
}

const manifestText = await readFile(manifestPath, 'utf8');
const entries = parseManifest(manifestText);

if (entries.length === 0) {
  throw new Error('Smith bundle manifest is empty: ' + manifestPath);
}

const parts = [
  '// Generated by compiler/build_smith_bundle.mjs\n',
  '// Source manifest: compiler/smith_LOAD_ORDER.txt\n',
  '\n',
];

for (const relPath of entries) {
  const absPath = path.resolve(compilerRoot, relPath);
  const source = await readFile(absPath, 'utf8');
  parts.push('// --- ' + relPath + ' ---\n');
  parts.push(source);
  if (!source.endsWith('\n')) parts.push('\n');
  parts.push('\n');
}

await mkdir(path.dirname(outPath), { recursive: true });
await writeFile(outPath, parts.join(''), 'utf8');

console.log('wrote ' + path.relative(compilerRoot, outPath));
