// ── Block-based module compiler ─────────────────────────────────────
// Handles <module name> with <types>, <ffi>, <state>, <functions> blocks.
// Each block tag is parsed independently — no ambiguity, no lookahead.

// Module-level state for the current compilation (set by compileModBlock)
var _modEnumVariants = [];
var _modFfiSymbols = {}; // symbol → prefix (e.g. 'socket' → 'posix')
var _modStateVars = []; // state variable names — don't redeclare as locals
var _modImportedNames = []; // imported type/value names available in module scope
var _modFnPtrParams = {}; // fnName → { paramIdx: true } for pointer params

function modTranspileType(ts) {
  const t = ts.trim();
  // Type? → optional (suffix form used by intent syntax)
  if (t.endsWith('?')) return '?' + modTranspileType(t.slice(0, -1));
  if (t === 'int') return 'i64';
  if (t === 'i32') return 'i32';
  if (t === 'i64') return 'i64';
  if (t === 'u8') return 'u8';
  if (t === 'u16') return 'u16';
  if (t === 'u32') return 'u32';
  if (t === 'u64') return 'u64';
  if (t === 'usize') return 'usize';
  if (t === 'f32') return 'f32';
  if (t === 'f64') return 'f64';
  if (t === 'float') return 'f32';
  if (t === 'number') return 'i64';
  if (t === 'bool' || t === 'boolean') return 'bool';
  if (t === 'string') return '[]const u8';
  if (t === 'void') return 'void';
  if (t.startsWith('fn(') || t.startsWith('fn (')) return modTranspileFnType(t);
  // ?Type → optional
  if (t.startsWith('?')) return '?' + modTranspileType(t.slice(1));
  // !Type → error union
  if (t.startsWith('!')) return '!' + modTranspileType(t.slice(1));
  // TypeName[N] → [N]TypeName (fixed array)
  const arrMatch = t.match(/^(\w+)\[([A-Za-z_]\w*|\d+)\]$/);
  if (arrMatch) return '[' + arrMatch[2] + ']' + modTranspileType(arrMatch[1]);
  // Type[] → slice — []Type
  if (t.endsWith('[]')) return '[]' + modTranspileType(t.slice(0, -2));
  // Pass through (user-defined types, Zig types)
  return t;
}

function modTranspileFnType(ts) {
  const m = ts.trim().match(/^fn\s*\((.*)\)\s*->\s*(.+)$/);
  if (!m) return ts.trim();
  return '*const fn (' + modTranspileParams(m[1].trim()) + ') ' + modTranspileType(m[2].trim());
}

function compileModBlock(source, file) {
  const basename = file.split('/').pop();
  _modStateVars = [];
  _modImportedNames = [];
  _modFnPtrParams = {};

  // Extract module name
  const moduleMatch = source.match(/<module\s+(\w+)>/);
  const moduleName = moduleMatch ? moduleMatch[1] : 'unknown';

  let out = '//! Generated by Smith (mod mode) — do not edit\n';
  out += '//! Source: ' + basename + '\n';
  out += '//! Module: ' + moduleName + '\n\n';
  out += 'const std = @import("std");\n';

  // Extract <imports> block → named @import bindings
  const importsMatch = extractModBlock(source, 'imports');
  if (importsMatch) {
    out += emitImportsBlock(importsMatch);
  }

  // Collect known type and enum variant names for context-aware codegen
  const typeNames = [];
  const enumVariants = {}; // typeName → [variant1, variant2, ...]
  const allVariants = []; // flat list of all enum variant names

  // Extract <ffi> block → import declarations
  const ffiMatch = extractModBlock(source, 'ffi');
  if (ffiMatch) {
    out += emitFfiBlock(ffiMatch);
  }

  // Extract <types> block
  const typesMatch = extractModBlock(source, 'types');
  if (typesMatch) {
    out += emitTypesBlock(typesMatch, typeNames, enumVariants, allVariants);
  } else {
    out += '\n'; // blank line after imports when no types block
  }
  // Set module-level enum variants for expression transpiling
  _modEnumVariants = allVariants;

  // Extract <const> block
  const constMatch = extractModBlock(source, 'const');
  if (constMatch) {
    out += emitConstBlock(constMatch, typeNames);
  }

  // Extract <state> block
  const stateMatch = extractModBlock(source, 'state');
  if (stateMatch) {
    out += emitStateBlock(stateMatch, typeNames);
  }

  // Extract <functions> block
  const fnMatch = extractModBlock(source, 'functions');
  if (fnMatch) {
    prescanModFunctionSigs(fnMatch);
    out += emitFunctionsBlock(fnMatch, typeNames, allVariants);
  }

  // Trim trailing whitespace but keep final newline
  return out.replace(/\n+$/, '\n');
}

function extractModBlock(source, tag) {
  const re = new RegExp('^\\s*<' + tag + '>\\s*\\n([\\s\\S]*?)^\\s*<\\/' + tag + '>\\s*$', 'm');
  const m = source.match(re);
  return m ? m[1] : null;
}

function emitImportsBlock(content) {
  let out = '';
  _modImportedNames = [];
  const lines = content.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line || line.startsWith('//')) continue;
    const m = line.match(/^(\w+)\s+from\s+"([^"]+)"$/);
    if (!m) continue;
    const name = m[1];
    const path = m[2];
    _modImportedNames.push(name);
    out += 'const ' + name + ' = @import("' + path + '").' + name + ';\n';
  }
  return out;
}

function emitFfiBlock(content) {
  let out = '';
  _modFfiSymbols = {};
  const lines = content.split('\n');
  const imports = {}; // lib → prefix
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line || line.startsWith('//')) continue;
    const m = line.match(/^(\w+)\s+@\("([^"]+)"(?:\s*,\s*"([^"]+)")?\)/);
    if (m) {
      const symbol = m[1];
      const lib = m[2];
      const actualFn = m[3] || symbol;
      // Determine prefix from lib
      var prefix;
      if (lib.startsWith('std.')) {
        const parts = lib.split('.');
        prefix = parts[parts.length - 1];
      } else {
        prefix = lib.replace(/[^a-zA-Z0-9]/g, '_');
      }
      if (!imports[lib]) imports[lib] = prefix;
      _modFfiSymbols[symbol] = { prefix: prefix, fn: actualFn };
    }
  }
  for (const lib in imports) {
    if (lib.startsWith('std.')) {
      const parts = lib.split('.');
      out += 'const ' + parts[parts.length - 1] + ' = std.' + parts.slice(1).join('.') + ';\n';
    } else {
      out += 'const ' + imports[lib] + ' = @cImport({ @cInclude("' + lib + '"); });\n';
    }
  }
  return out;
}

function emitTypesBlock(content, typeNames, enumVariants, allVariants) {
  if (!enumVariants) enumVariants = {};
  if (!allVariants) allVariants = [];
  let out = '\n';
  const lines = content.split('\n');
  let i = 0;

  while (i < lines.length) {
    const line = lines[i].trim();

    // Skip empty lines and comments
    if (!line || line.startsWith('//')) { i++; continue; }

    // Type declaration: Name: ...
    const declMatch = line.match(/^([A-Z]\w*):\s*(.*)$/);
    if (!declMatch) { i++; continue; }

    const name = declMatch[1];
    const rest = declMatch[2].trim();
    typeNames.push(name);

    // Tagged union: Name: union { ... }
    if (rest.startsWith('union')) {
      const bodyLines = [];
      const hasOpenBrace = rest.includes('{');
      if (hasOpenBrace) {
        const after = rest.replace(/^union\s*\{\s*/, '').trim();
        if (after && after !== '}') bodyLines.push(after);
      }
      i++;
      while (i < lines.length) {
        const l = lines[i].trim();
        if (l === '}' || l === '};') break;
        if (l) bodyLines.push(l);
        i++;
      }
      i++; // skip closing }
      out += emitUnionDecl(name, bodyLines, typeNames);
      continue;
    }

    // Struct: Name: { ... }
    if (rest.startsWith('{')) {
      const bodyLines = [];
      const after = rest.slice(1).trim();
      if (after && after !== '}') bodyLines.push(after);
      i++;
      while (i < lines.length) {
        const l = lines[i].trim();
        if (l === '}' || l === '};') break;
        if (l) bodyLines.push(l);
        i++;
      }
      i++; // skip closing }
      out += emitStructDecl(name, bodyLines, typeNames);
      continue;
    }

    // Enum: Name: val1 | val2 | val3
    if (rest.includes('|')) {
      out += emitEnumDecl(name, rest, allVariants);
      i++;
      continue;
    }

    // Type alias: Name: fn(...) -> Ret  OR  Name: SomeOtherType
    out += emitTypeAliasDecl(name, rest);
    i++;
    continue;

  }

  return out;
}

function emitTypeAliasDecl(name, rest) {
  return 'pub const ' + name + ' = ' + modTranspileType(rest) + ';\n\n';
}

function emitEnumDecl(name, rest, allVariants) {
  const variants = rest.split('|').map(function(v) { return v.trim(); }).filter(Boolean);
  if (allVariants) { for (let v = 0; v < variants.length; v++) allVariants.push(variants[v]); }
  let out = 'pub const ' + name + ' = enum {\n';
  for (let v = 0; v < variants.length; v++) {
    out += '    ' + zigEscape(variants[v]) + ',\n';
  }
  out += '};\n\n';
  return out;
}

function emitStructDecl(name, bodyLines, typeNames) {
  let out = 'pub const ' + name + ' = struct {\n';
  for (let b = 0; b < bodyLines.length; b++) {
    const field = bodyLines[b].replace(/,\s*$/, '').trim();
    const fm = field.match(/^(\w+):\s*([^=]+?)(?:\s*=\s*(.+))?$/);
    if (!fm) continue;
    const fname = fm[1];
    const rawType = fm[2].trim();
    const ftype = modTranspileType(rawType);
    let fdefault = fm[3] ? fm[3].trim() : null;

    if (fdefault !== null) {
      fdefault = modTranspileDefault(fdefault, ftype, typeNames);
    } else {
      // Infer defaults for fields without explicit default
      fdefault = inferDefault(rawType, ftype, typeNames);
    }

    out += '    ' + fname + ': ' + ftype;
    if (fdefault !== null) out += ' = ' + fdefault;
    out += ',\n';
  }
  out += '};\n\n';
  return out;
}

function inferDefault(rawType, zigType, typeNames) {
  // string → ""
  if (rawType === 'string') return '""';
  // Type? → null
  if (rawType.endsWith('?')) return 'null';
  // ?Type → null
  if (rawType.startsWith('?')) return 'null';
  // Type[] → empty slice
  if (rawType.endsWith('[]')) return '&.{}';
  // Type[N] where Type is a known struct → [_]Type{.{}} ** N
  const arrMatch = rawType.match(/^(\w+)\[([A-Za-z_]\w*|\d+)\]$/);
  if (arrMatch) {
    const elemType = arrMatch[1];
    const count = arrMatch[2];
    if (typeNames.indexOf(elemType) !== -1) return '[_]' + modTranspileType(elemType) + '{.{}} ** ' + count;
    // Primitive array like u8[65536] → undefined
    return 'undefined';
  }
  // Known struct type → .{}
  if (typeNames.indexOf(rawType) !== -1) return '.{}';
  if (_modImportedNames.indexOf(rawType) !== -1) return '.{}';
  return null;
}

function emitUnionDecl(name, bodyLines, typeNames) {
  let out = 'pub const ' + name + ' = union(enum) {\n';
  for (let b = 0; b < bodyLines.length; b++) {
    const field = bodyLines[b].replace(/,\s*$/, '').trim();
    const fm = field.match(/^(\w+):\s*(.+)$/);
    if (!fm) continue;
    out += '    ' + zigEscape(fm[1]) + ': ' + modTranspileType(fm[2].trim()) + ',\n';
  }
  out += '};\n\n';
  return out;
}

function modTranspileDefault(val, zigType, typeNames) {
  const v = val.trim();
  // Boolean
  if (v === 'true' || v === 'false') return v;
  // Null
  if (v === 'null') return 'null';
  if (v === 'none' && zigType && zigType.startsWith('?')) return 'null';
  // Numeric
  if (/^-?\d+(\.\d+)?$/.test(v)) return v;
  // String literal
  if (v.startsWith('"') || v.startsWith("'")) return v.replace(/'/g, '"');
  // Enum variant — identifier (any case) that isn't a type name → prefix with .
  if (/^\w+$/.test(v) && typeNames.indexOf(v) === -1) return '.' + v;
  // Struct init
  if (v === '{}') return '.{}';
  return v;
}

function emitStateBlock(content, typeNames) {
  let out = '';
  const lines = content.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line || line.startsWith('//')) continue;
    // name: Type[N] = default  OR  name: Type = default  OR  name: Type
    const m = line.match(/^(\w+):\s*([^=]+?)(?:\s*=\s*(.+))?$/);
    if (!m) continue;
    const vname = m[1];
    _modStateVars.push(vname);
    const rawType = m[2].trim();
    const vdefault = m[3] ? m[3].trim() : null;
    const zigType = modTranspileType(rawType);

    out += 'var ' + vname + ': ' + zigType;
    // Array types get zero-init
    const arrM = rawType.match(/^(\w+)\[([A-Za-z_]\w*|\d+)\]$/);
    if (arrM) {
      if (typeNames.indexOf(arrM[1]) !== -1) {
        out += ' = [_]' + modTranspileType(arrM[1]) + '{.{}} ** ' + arrM[2];
      } else {
        out += ' = undefined';
      }
    } else if (zigType.startsWith('?')) {
      out += ' = null';
    } else if (vdefault !== null) {
      out += ' = ' + modTranspileDefault(vdefault, zigType, typeNames);
    } else if (rawType.endsWith('[]')) {
      out += ' = &.{}';
    } else if (_modImportedNames.indexOf(rawType) !== -1) {
      out += ' = .{}';
    } else {
      out += ' = .{}';
    }
    out += ';\n';
  }
  return out;
}

function emitConstBlock(content, typeNames) {
  let out = '';
  const lines = content.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line || line.startsWith('//')) continue;
    const m = line.match(/^(\w+):\s*([^=]+?)\s*=\s*(.+)$/);
    if (!m) continue;
    out += 'const ' + m[1] + ': ' + modTranspileType(m[2].trim()) + ' = ' + modTranspileDefault(m[3].trim(), modTranspileType(m[2].trim()), typeNames) + ';\n';
  }
  return out;
}

function emitFunctionsBlock(content, typeNames, allVariants) {
  let out = '\n';
  // Split into individual functions by detecting signatures at top-level indent.
  const lines = content.split('\n');
  const funcs = [];
  let current = null;
  for (let i = 0; i < lines.length; i++) {
    const raw = lines[i];
    const trimmed = raw.trim();
    const indent = raw.match(/^(\s*)/)[1].length;
    const isSig = indent <= 4 && trimmed.match(/^\w+\([^)]*\)\s*(?::\s*.+)?$/);
    if (isSig) {
      if (current) funcs.push(current);
      current = { sig: trimmed, body: [] };
      continue;
    }
    if (current) current.body.push(raw);
  }
  if (current) funcs.push(current);
  for (let f = 0; f < funcs.length; f++) out += emitOneFunction(funcs[f].sig, funcs[f].body, typeNames, allVariants);
  return out;
}
function emitOneFunction(sig, rawBodyLines, typeNames, allVariants) {
  const fnMatch = sig.match(/^(\w+)\(([^)]*)\)\s*(?::\s*(\S+))?\s*$/);
  if (!fnMatch) return '';
  const fname = fnMatch[1]; const params = fnMatch[2]; const ret = fnMatch[3] || 'void';
  const paramInfo = parseModParams(params);
  const zigParams = emitModParams(paramInfo, fname); const zigRet = modTranspileType(ret);
  const paramNames = paramInfo.map(function(p) { return p.name; });
  const ptrVars = paramInfo.filter(function(p) { return p.isPtr; }).map(function(p) { return p.name; });
  const bodyLines = [];
  for (let i = 0; i < rawBodyLines.length; i++) {
    const raw = rawBodyLines[i]; const trimmed = raw.trim();
    if (!trimmed) continue;
    if (trimmed.startsWith('//')) continue;
    bodyLines.push({ indent: raw.match(/^(\s*)/)[1].length, text: trimmed });
  }
  if (bodyLines.length === 1 && bodyLines[0].text.match(/return .+\.map\(/)) {
    return emitMapFunction(fname, zigParams, zigRet, bodyLines[0].text, typeNames);
  }
  const ctx = {
    knownVars: (_modStateVars || []).concat(paramNames),
    localNames: [],
    ptrVars: ptrVars,
    narrowedVars: [],
  };
  let out = 'pub fn ' + fname + '(' + zigParams + ') ' + zigRet + ' {\n';
  out += emitModBody(bodyLines, 0, typeNames, 1, allVariants, zigRet, ctx);
  out += '}\n\n';
  return out;
}
function modTranspileParams(params) {
  return emitModParams(parseModParams(params), null);
}

function parseModParams(params) {
  if (!params.trim()) return [];
  return params.split(',').map(function(p) {
    const m = p.trim().match(/^(\w+):\s*(.+)$/);
    if (!m) return null;
    const rawType = m[2].trim();
    return {
      name: m[1],
      rawType: rawType,
      isPtr: isModPointerParamType(rawType),
    };
  }).filter(Boolean);
}

function emitModParams(paramInfo, fnName) {
  return paramInfo.map(function(p, idx) {
    let zigType = modTranspileType(p.rawType);
    if (p.isPtr) {
      if (fnName) registerModPtrParam(fnName, idx);
      zigType = '*' + zigType;
    }
    return p.name + ': ' + zigType;
  }).join(', ');
}

function isModPointerParamType(rawType) {
  return rawType.trim() === 'Node';
}

function registerModPtrParam(fnName, idx) {
  if (!_modFnPtrParams[fnName]) _modFnPtrParams[fnName] = {};
  _modFnPtrParams[fnName][idx] = true;
}

function prescanModFunctionSigs(content) {
  _modFnPtrParams = {};
  const lines = content.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const raw = lines[i];
    const trimmed = raw.trim();
    const indent = raw.match(/^(\s*)/)[1].length;
    const sigMatch = indent <= 4 ? trimmed.match(/^(\w+)\(([^)]*)\)\s*(?::\s*(\S+))?\s*$/) : null;
    if (!sigMatch) continue;
    const fnName = sigMatch[1];
    const paramInfo = parseModParams(sigMatch[2]);
    for (let p = 0; p < paramInfo.length; p++) {
      if (paramInfo[p].isPtr) registerModPtrParam(fnName, p);
    }
  }
}

function emitFunctionBody(lines, typeNames, depth) {
  let out = '';
  const indent = '    '.repeat(depth);
  let i = 0;

  while (i < lines.length) {
    const line = lines[i];
    if (!line) { i++; continue; }

    // Guard: cond ? stop : go → if (cond) return ...;
    const guardMatch = line.match(/^(.+)\s+\?\s+stop\s*:\s*go$/);
    if (guardMatch) {
      out += indent + 'if (' + modTranspileExpr(guardMatch[1]) + ') return ' + modGuardReturn(depth) + ';\n';
      i++;
      continue;
    }

    // Return
    if (line.startsWith('return ')) {
      out += indent + 'return ' + modTranspileExpr(line.slice(7)) + ';\n';
      i++;
      continue;
    }

    // Switch: switch name:
    const switchMatch = line.match(/^switch\s+(\w+):$/);
    if (switchMatch) {
      const switchVar = switchMatch[1];
      out += indent + 'switch (' + switchVar + ') {\n';
      i++;
      // Collect arms until de-indent
      while (i < lines.length && lines[i]) {
        const arm = lines[i];
        // Arm: variant:
        const armMatch = arm.match(/^(\w+):$/);
        if (armMatch) {
          out += indent + '    .' + armMatch[1] + ' => {\n';
          i++;
          // Collect arm body
          const armBody = [];
          while (i < lines.length && lines[i] && !lines[i].match(/^\w+:$/) && !lines[i].startsWith('return ')) {
            armBody.push(lines[i]);
            i++;
          }
          // Check if next line is return (belongs to outer scope, not arm)
          out += emitArmBody(armBody, typeNames, depth + 2);
          out += indent + '    },\n';
          continue;
        }
        break;
      }
      out += indent + '}\n';
      continue;
    }

    // For loop: for array as item:
    const forMatch = line.match(/^for\s+(.+?)\s+as\s+(\w+):$/);
    if (forMatch) {
      const arrExpr = forMatch[1];
      const itemVar = forMatch[2];
      out += indent + 'var _i: usize = 0;\n';
      // Determine the count expression
      const rangeMatch = arrExpr.match(/^(\w+)\[(\d+)\.\.(\w+)\]$/);
      if (rangeMatch) {
        out += indent + 'while (_i < ' + rangeMatch[3] + ') : (_i += 1) {\n';
      } else {
        out += indent + 'while (_i < ' + arrExpr + '.len) : (_i += 1) {\n';
      }
      i++;
      // Collect body
      const forBody = [];
      while (i < lines.length && lines[i] && !lines[i].match(/^\w+\(/) && !lines[i].match(/^return /)) {
        forBody.push(lines[i]);
        i++;
      }
      out += emitForBody(forBody, arrExpr, itemVar, typeNames, depth + 1);
      out += indent + '}\n';
      continue;
    }

    // Assignment: target = { ... } → struct init
    const structAssign = line.match(/^(.+?)\s*=\s*\{(.+)\}\s*$/);
    if (structAssign) {
      const target = modTranspileExpr(structAssign[1]);
      const fields = structAssign[2].split(',').map(function(f) {
        const kv = f.trim().match(/^(\w+):\s*(.+)$/);
        if (kv) return '.' + kv[1] + ' = ' + modTranspileExpr(kv[2].trim());
        return f.trim();
      }).join(', ');
      out += indent + target + ' = .{ ' + fields + ' };\n';
      i++;
      continue;
    }

    // Assignment: target = expr
    const assignMatch = line.match(/^(.+?)\s*=\s*(.+)$/);
    if (assignMatch && !assignMatch[1].includes('(')) {
      const target = modTranspileExpr(assignMatch[1]);
      const val = modTranspileExpr(assignMatch[2]);
      out += indent + target + ' = ' + val + ';\n';
      i++;
      continue;
    }

    // Bare expression / statement
    out += indent + modTranspileExpr(line) + ';\n';
    i++;
  }

  return out;
}

function emitArmBody(lines, typeNames, depth) {
  let out = '';
  const indent = '    '.repeat(depth);
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line) continue;
    // Ternary assignment: cond ? target = val1 : target = val2
    const ternAssign = line.match(/^(.+?)\s+and\s+(.+)$/);
    // cond\n  ? expr1\n  : expr2 — check for multi-line ternary
    const condAssign = line.match(/^(.+)\s*\?\s*(.+?)\s*:\s*(.+)$/);
    if (condAssign) {
      const cond = modTranspileExpr(condAssign[1]);
      const ifTrue = condAssign[2].trim();
      const ifFalse = condAssign[3].trim();
      // Both sides are assignments
      const trueAssign = ifTrue.match(/^(.+?)\s*=\s*(.+)$/);
      const falseAssign = ifFalse.match(/^(.+?)\s*=\s*(.+)$/);
      if (trueAssign && falseAssign) {
        out += indent + 'if (' + cond + ') {\n';
        out += indent + '    ' + modTranspileExpr(trueAssign[1]) + ' = ' + modTranspileExpr(trueAssign[2]) + ';\n';
        out += indent + '} else {\n';
        out += indent + '    ' + modTranspileExpr(falseAssign[1]) + ' = ' + modTranspileExpr(falseAssign[2]) + ';\n';
        out += indent + '}\n';
        continue;
      }
    }
    // Fallback: assignment or expression
    const assignMatch = line.match(/^(.+?)\s*=\s*(.+)$/);
    if (assignMatch && !assignMatch[1].includes('(')) {
      out += indent + modTranspileExpr(assignMatch[1]) + ' = ' + modTranspileExpr(assignMatch[2]) + ';\n';
    } else {
      out += indent + modTranspileExpr(line) + ';\n';
    }
  }
  return out;
}

function emitForBody(lines, arrExpr, itemVar, typeNames, depth) {
  let out = '';
  const indent = '    '.repeat(depth);
  // Determine the array access pattern
  const rangeMatch = arrExpr.match(/^(\w+)\[(\d+)\.\.(\w+)\]$/);
  const baseArr = rangeMatch ? rangeMatch[1] : arrExpr;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (!line) continue;
    // cond ? action; return : continue
    const condMatch = line.match(/^(.+?)\s*\?\s*(.+?)\s*;\s*return\s*:\s*continue$/);
    if (condMatch) {
      out += indent + 'if (' + modTranspileForExpr(condMatch[1], baseArr, itemVar) + ') {\n';
      // Parse the action part (may be multiple semicoloned statements)
      const actions = condMatch[2].split(';').map(function(a) { return a.trim(); }).filter(Boolean);
      for (let a = 0; a < actions.length; a++) {
        const am = actions[a].match(/^(.+?)\s*=\s*(.+)$/);
        if (am) {
          out += indent + '    ' + modTranspileForExpr(am[1], baseArr, itemVar) + ' = ' + modTranspileForExpr(am[2], baseArr, itemVar) + ';\n';
        } else if (actions[a].startsWith('return')) {
          out += indent + '    return ' + modTranspileForExpr(actions[a].slice(7), baseArr, itemVar) + ';\n';
        } else {
          out += indent + '    ' + modTranspileForExpr(actions[a], baseArr, itemVar) + ';\n';
        }
      }
      out += indent + '}\n';
      continue;
    }
    // Regular line
    const assignMatch = line.match(/^(.+?)\s*=\s*(.+)$/);
    if (assignMatch) {
      out += indent + modTranspileForExpr(assignMatch[1], baseArr, itemVar) + ' = ' + modTranspileForExpr(assignMatch[2], baseArr, itemVar) + ';\n';
    } else {
      out += indent + modTranspileForExpr(line, baseArr, itemVar) + ';\n';
    }
  }
  return out;
}

function modTranspileForExpr(expr, baseArr, itemVar) {
  // Replace item.field with baseArr[_i].field
  let e = expr.replace(new RegExp('\\b' + itemVar + '\\.', 'g'), baseArr + '[_i].');
  // Replace bare item with baseArr[_i]
  e = e.replace(new RegExp('\\b' + itemVar + '\\b', 'g'), baseArr + '[_i]');
  return modTranspileExpr(e);
}

function modTranspileExpr(expr, ctx) {
  let e = expr.trim();
  // exact → ==
  e = e.replace(/\bexact\b/g, '==');
  // Prefix known enum variants with . when used as values (after = or ==)
  // Do NOT prefix when used as LHS of comparison (e.g. paused == true where paused is a var)
  if (_modEnumVariants && _modEnumVariants.length > 0) {
    const localNames = (ctx && ctx.localNames) || [];
    for (let v = 0; v < _modEnumVariants.length; v++) {
      var vname = _modEnumVariants[v];
      if (localNames.indexOf(vname) !== -1) continue;
      // Match after = (assignment or comparison RHS), comma, semicolon, open paren
      // Use capture group instead of variable-length lookbehind for QuickJS compat
      e = e.replace(new RegExp('([=,;(] ?)' + vname + '(?=[\\s;,)=]|$)', 'g'), '$1.' + vname);
      // Also match after == with space
      e = e.replace(new RegExp('(== ?)' + vname + '(?=[\\s;,)=]|$)', 'g'), '$1.' + vname);
      // Bare variant as entire expression
      if (e === vname) e = '.' + vname;
    }
  }
  // and / or
  e = e.replace(/\band\b/g, 'and');
  e = e.replace(/\bor\b/g, 'or');
  // !== and === → != and ==
  e = e.replace(/===/g, '==');
  e = e.replace(/!==/g, '!=');
  // || → or, && → and
  e = e.replace(/\s*\|\|\s*/g, ' or ');
  e = e.replace(/\s*&&\s*/g, ' and ');
  // ?? → orelse
  e = e.replace(/\s*\?\?\s*/g, ' orelse ');
  const ternaryExpr = rewriteExpressionTernary(e, ctx);
  if (ternaryExpr) e = ternaryExpr;
  // ── Stdlib method mapping ──
  // Pattern: match complex LHS (words, dots, brackets) before method call
  // x.indexOf(str) → std.mem.indexOf(u8, x, str) orelse x.len
  e = e.replace(/([\w\[\]_.]+)\.indexOf\(([^)]+)\)/g, function(_, obj, arg) {
    var a = arg.trim().replace(/'/g, '"');
    return 'std.mem.indexOf(u8, ' + obj + ', ' + a + ') orelse ' + obj + '.len';
  });
  // x.indexOfChar(c) → std.mem.indexOfScalar(u8, x, c) orelse x.len
  e = e.replace(/([\w\[\]_.]+)\.indexOfChar\(([^)]+)\)/g, function(_, obj, arg) {
    return 'std.mem.indexOfScalar(u8, ' + obj + ', ' + arg.trim() + ') orelse ' + obj + '.len';
  });
  // a.eql(b) → std.mem.eql(u8, a, b)
  e = e.replace(/([\w\[\]_.]+)\.eql\(([^)]+)\)/g, function(_, obj, arg) {
    return 'std.mem.eql(u8, ' + obj + ', ' + arg.trim() + ')';
  });
  // parseInt(str) → std.fmt.parseInt(i32, str, 10) catch 0
  e = e.replace(/parseInt\(([^)]+)\)/g, function(_, arg) {
    return 'std.fmt.parseInt(i32, ' + arg.trim() + ', 10) catch 0';
  });
  // ── FFI call prefixing ──
  if (_modFfiSymbols) {
    for (var sym in _modFfiSymbols) {
      var info = _modFfiSymbols[sym];
      e = e.replace(new RegExp('(?<!\\w\\.)\\b' + sym + '\\(', 'g'), info.prefix + '.' + info.fn + '(');
    }
  }
  // ── Posix constant mapping ──
  // AF_INET → posix.AF.INET, SOCK_STREAM → posix.SOCK.STREAM, O_RDONLY → posix.O.RDONLY, etc.
  if (_modFfiSymbols) {
    // Check if any FFI import is from std.posix
    var hasPosix = false;
    for (var s in _modFfiSymbols) { if (_modFfiSymbols[s].prefix === 'posix') hasPosix = true; }
    if (hasPosix) {
      // Map UPPER_CASE constants: PREFIX_REST → posix.PREFIX.REST
      e = e.replace(/\b(AF|SOCK|IPPROTO|O|POLL|MSG|SO|SOL|SHUT|F|FD|SEEK|MAP|PROT|CLOCK|SIG|SA|S_I|EPOLL|IN)_([A-Z0-9_]+)\b/g, function(_, prefix, rest) {
        return 'posix.' + prefix + '.' + rest;
      });
    }
  }
  // ── String concatenation → std.fmt.bufPrint ──
  // Only trigger when expression contains a string literal with +
  if (e.indexOf(" + ") !== -1 && e.indexOf("'") !== -1) {
    var bufPrint = transpileStringConcat(e);
    if (bufPrint) return bufPrint;
  }
  return rewriteKnownFunctionCalls(e, ctx);
}

function transpileStringConcat(expr) {
  // Split on + but respect quoted strings
  var parts = [];
  var cur = '';
  var inStr = false;
  for (var c = 0; c < expr.length; c++) {
    if (expr[c] === "'" && !inStr) { inStr = true; cur += expr[c]; continue; }
    if (expr[c] === "'" && inStr) { inStr = false; cur += expr[c]; continue; }
    if (!inStr && expr[c] === '+' && (c === 0 || expr[c-1] === ' ') && (c + 1 >= expr.length || expr[c+1] === ' ')) {
      parts.push(cur.trim());
      cur = '';
      continue;
    }
    cur += expr[c];
  }
  if (cur.trim()) parts.push(cur.trim());
  if (parts.length < 2) return null;
  // Build format string and args
  var fmt = '';
  var args = [];
  for (var p = 0; p < parts.length; p++) {
    var part = parts[p];
    if (part.startsWith("'") && part.endsWith("'")) {
      // String literal — inline into format
      fmt += part.slice(1, -1);
    } else {
      // Variable — determine format specifier
      // If it looks numeric (or is a .len or bare int var), use {d}
      if (part.match(/\.len$/) || part.match(/^-?\d/) || part === 'code' || part.match(/count|size|len|num|idx|id$/i)) {
        fmt += '{d}';
      } else {
        fmt += '{s}';
      }
      args.push(part);
    }
  }
  var argStr = args.length === 1 ? args[0] : ' ' + args.join(', ') + ' ';
  if (args.length === 1) argStr = args[0];
  else argStr = ' ' + args.join(', ') + ' ';
  return 'std.fmt.bufPrint(&buf, "' + fmt + '", .{' + argStr + '}) catch ""';
}

function emitModBody(lines, startIdx, typeNames, depth, allVariants, retType, ctx) {
  let out = '';
  const ind = '    '.repeat(depth);
  const knownVars = ctx.knownVars || [];
  let narrowedVars = (ctx.narrowedVars || []).slice();
  var guardRetVal = 'return';
  if (retType && retType !== 'void') {
    if (retType === 'bool') guardRetVal = 'return false';
    else if (retType === 'i32' || retType === 'i64') guardRetVal = 'return -1';
    else if (retType === 'u8' || retType === 'u16' || retType === 'u32' || retType === 'usize') guardRetVal = 'return 0';
    else if (retType === 'f32' || retType === 'f64') guardRetVal = 'return 0.0';
    else if (retType.startsWith('[]') || retType.startsWith('?') || retType.startsWith('*')) guardRetVal = 'return null';
    else if (retType.startsWith('!')) guardRetVal = 'return error.GuardFailed';
    else guardRetVal = 'return undefined';
  }
  let i = startIdx;
  while (i < lines.length) {
    const L = lines[i]; const text = L.text;
    const activeCtx = extendModCtx(ctx, { narrowedVars: narrowedVars });
    if (!text) { i++; continue; }
    // Guard: cond ? stop : go
    const guardMatch = text.match(/^(.+?)\s+\?\s+stop\s*:\s*go$/);
    if (guardMatch) { out += ind + 'if (' + modTranspileExpr(applyOptionalUnwraps(guardMatch[1], narrowedVars), activeCtx) + ') ' + guardRetVal + ';\n'; i++; continue; }
    // Return
    if (text.startsWith('return ')) { out += ind + 'return ' + modTranspileValue(text.slice(7), activeCtx) + ';\n'; i++; continue; }
    // Struct init: target = { field: val }
    const structAssign = text.match(/^(.+?)\s*=\s*\{(.+)\}\s*$/);
    if (structAssign && !isComparison(structAssign[1])) {
      const target = structAssign[1].trim();
      out += ind + target + ' = ' + transpileStructLiteral(structAssign[2]) + ';\n'; i++; continue;
    }
    // Assignment: target = expr (not >= <= == !=)
    const assignMatch = text.match(/^([^=!<>]+?)\s*=\s*([^=].*)$/);
    if (assignMatch && !assignMatch[1].includes('(') && !isComparison(assignMatch[1])) {
      out += emitSingleStatement(text, depth, typeNames, lines, i, guardRetVal, activeCtx);
      i++; continue;
    }
    // Single-line ternary
    const ternaryMatch = text.match(/^(.+?)\s*\?\s*(.+?)\s*:\s*(.+)$/);
    if (ternaryMatch) {
      const cond = ternaryMatch[1];
      const thenCtx = extendModCtx(ctx, { narrowedVars: narrowedVars.concat(extractNonNullVars(cond)) });
      const elseCtx = extendModCtx(ctx, { narrowedVars: narrowedVars.concat(extractNullGuardedVars(cond)) });
      const elseExpr = ternaryMatch[3].trim();
      out += ind + 'if (' + modTranspileExpr(applyOptionalUnwraps(cond, narrowedVars), activeCtx) + ') {\n';
      out += emitInlineStatements(ternaryMatch[2], depth + 1, typeNames, lines, i, guardRetVal, thenCtx);
      if (elseExpr !== 'go' && !(activeCtx.inLoop && elseExpr === 'continue')) {
        out += ind + '} else {\n';
        out += emitInlineStatements(ternaryMatch[3], depth + 1, typeNames, lines, i, guardRetVal, elseCtx);
      }
      out += ind + '}\n';
      if (elseExpr === 'go' && isEarlyExitBranch(ternaryMatch[2])) {
        narrowedVars = addUniqueVars(narrowedVars, extractNullGuardedVars(cond));
      }
      i++; continue;
    }
    // Multi-line ternary
    if (i + 2 < lines.length && lines[i + 1].text.startsWith('? ') && lines[i + 2].text.startsWith(': ')) {
      const cond = text;
      const thenExpr = lines[i + 1].text.slice(2).trim();
      const elseExpr = lines[i + 2].text.slice(2).trim();
      const thenCtx = extendModCtx(ctx, { narrowedVars: narrowedVars.concat(extractNonNullVars(cond)) });
      const elseCtx = extendModCtx(ctx, { narrowedVars: narrowedVars.concat(extractNullGuardedVars(cond)) });
      out += ind + 'if (' + modTranspileExpr(applyOptionalUnwraps(cond, narrowedVars), activeCtx) + ') {\n';
      out += emitInlineStatements(thenExpr, depth + 1, typeNames, lines, i, guardRetVal, thenCtx);
      if (elseExpr !== 'go' && !(activeCtx.inLoop && elseExpr === 'continue')) {
        out += ind + '} else {\n';
        out += emitInlineStatements(elseExpr, depth + 1, typeNames, lines, i, guardRetVal, elseCtx);
      }
      out += ind + '}\n';
      if (elseExpr === 'go' && isEarlyExitBranch(thenExpr)) {
        narrowedVars = addUniqueVars(narrowedVars, extractNullGuardedVars(cond));
      }
      i += 3; continue;
    }
    // Switch
    const switchMatch = text.match(/^switch\s+(.+):$/);
    if (switchMatch) {
      out += ind + 'switch (' + switchMatch[1] + ') {\n'; i++;
      while (i < lines.length) {
        const armMatch = lines[i].text.match(/^(\w+):$/);
        if (!armMatch) break;
        const armIndent = lines[i].indent;
        out += ind + '    .' + armMatch[1] + ' => {\n'; i++;
        const armBody = [];
        while (i < lines.length && !lines[i].text.match(/^\w+:$/) && lines[i].indent > armIndent) { armBody.push(lines[i]); i++; }
        out += emitArmBodyV2(armBody, typeNames, depth + 2, extendModCtx(ctx, { narrowedVars: narrowedVars }));
        out += ind + '    },\n';
      }
      out += ind + '}\n'; continue;
    }
    // For loop
    const forMatch = text.match(/^for\s+(.+?)\s+as\s+(\w+)(?:\s+at\s+(\w+))?:$/);
    if (forMatch) {
      const forIndent = L.indent; i++;
      const forBody = [];
      while (i < lines.length && lines[i].indent > forIndent) { forBody.push(lines[i]); i++; }
      out += emitForLoopV2(forMatch[1], forMatch[2], forMatch[3] || null, forBody, typeNames, depth, extendModCtx(ctx, { narrowedVars: narrowedVars })); continue;
    }
    out += emitSingleStatement(text, depth, typeNames, lines, i, guardRetVal, activeCtx); i++;
  }
  return out;
}
function isComparison(lhs) {
  const t = lhs.trim();
  return t.endsWith('>') || t.endsWith('<') || t.endsWith('!') || t.endsWith('=');
}
function inferTypeFromValue(val) {
  const v = val.trim();
  if (v === '0' || /^-?\d+$/.test(v)) return 'i32';
  if (/^-?\d+\.\d+$/.test(v)) return 'f32';
  if (v === 'true' || v === 'false') return 'bool';
  if (v.startsWith('"') || v.startsWith("'")) return '[]const u8';
  return null; // can't infer — don't declare as var
}
// Split struct literal fields respecting brace nesting, then transpile each
function transpileStructLiteral(inner) {
  // Split by commas that aren't inside nested { }
  const fields = [];
  let depth = 0; let cur = '';
  for (let c = 0; c < inner.length; c++) {
    if (inner[c] === '{') depth++;
    if (inner[c] === '}') depth--;
    if (inner[c] === ',' && depth === 0) { fields.push(cur.trim()); cur = ''; continue; }
    cur += inner[c];
  }
  if (cur.trim()) fields.push(cur.trim());
  // Transpile each field: key: value → .key = value
  const zigFields = fields.map(function(f) {
    const kv = f.match(/^(\w+):\s*(.+)$/);
    if (!kv) return f;
    const val = kv[2].trim();
    // Check if value is a nested struct literal { ... }
    const nestedMatch = val.match(/^\{(.+)\}$/);
    if (nestedMatch) return '.' + kv[1] + ' = ' + transpileStructLiteral(nestedMatch[1]);
    return '.' + kv[1] + ' = ' + modTranspileExpr(val);
  });
  return '.{ ' + zigFields.join(', ') + ' }';
}
// Emit a semicolon-separated statement list, each through modTranspileExpr
function emitStatementList(expr, ind) {
  var out = '';
  var stmts = expr.split(';').map(function(s) { return s.trim(); }).filter(Boolean);
  for (var s = 0; s < stmts.length; s++) {
    var stmt = stmts[s];
    var am = stmt.match(/^([^=!<>]+?)\s*=\s*([^=].*)$/);
    if (am) {
      var target = modTranspileExpr(am[1].trim());
      var val = modTranspileExpr(am[2].trim());
      var esc = target.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
      var inc = val.match(new RegExp('^' + esc + '\\s*\\+\\s*(.+)$'));
      var dec = !inc ? val.match(new RegExp('^' + esc + '\\s*-\\s*(.+)$')) : null;
      if (inc) { out += ind + target + ' += ' + inc[1] + ';\n'; }
      else if (dec) { out += ind + target + ' -= ' + dec[1] + ';\n'; }
      else { out += ind + target + ' = ' + val + ';\n'; }
    } else {
      out += ind + modTranspileExpr(stmt) + ';\n';
    }
  }
  return out;
}

function emitInlineStatements(expr, depth, typeNames, lines, lineIdx, guardRetVal, ctx) {
  let out = '';
  const stmts = expr.split(';').map(function(s) { return s.trim(); }).filter(Boolean);
  for (let s = 0; s < stmts.length; s++) {
    out += emitSingleStatement(stmts[s], depth, typeNames, lines, lineIdx, guardRetVal, ctx);
  }
  return out;
}

function emitSingleStatement(stmt, depth, typeNames, lines, lineIdx, guardRetVal, ctx) {
  const ind = '    '.repeat(depth);
  const knownVars = ctx.knownVars || [];
  const localNames = ctx.localNames || [];
  const narrowedVars = ctx.narrowedVars || [];
  const text = applyOptionalUnwraps(stmt.trim(), narrowedVars);
  if (!text || text === 'go') return '';
  if (text === 'continue') return ind + 'continue;\n';
  if (text === 'stop') return ind + guardRetVal + ';\n';
  if (text.startsWith('return ')) return ind + 'return ' + modTranspileValue(text.slice(7), ctx) + ';\n';

  const structAssign = text.match(/^(.+?)\s*=\s*\{(.+)\}\s*$/);
  if (structAssign && !isComparison(structAssign[1])) {
    const target = structAssign[1].trim();
    return ind + target + ' = ' + transpileStructLiteral(structAssign[2]) + ';\n';
  }

  const assignMatch = text.match(/^([^=!<>]+?)\s*=\s*([^=].*)$/);
  if (assignMatch && !assignMatch[1].includes('(') && !isComparison(assignMatch[1])) {
    const rawTarget = assignMatch[1].trim();
    const target = rawTarget;
    const val = modTranspileValue(assignMatch[2].trim(), ctx);

    if (/^\w+$/.test(rawTarget) && !rawTarget.includes('.') && !rawTarget.includes('[') && knownVars.indexOf(rawTarget) === -1) {
      knownVars.push(rawTarget);
      localNames.push(rawTarget);
      const inferredType = inferTypeFromValue(assignMatch[2].trim());
      const isMutable = localIsReassigned(rawTarget, lines, lineIdx);
      if (inferredType) {
        return ind + (isMutable ? 'var ' : 'const ') + target + ': ' + inferredType + ' = ' + val + ';\n';
      }
      return ind + (isMutable ? 'var ' : 'const ') + target + ' = ' + val + ';\n';
    }

    const esc = target.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
    const incMatch = val.match(new RegExp('^' + esc + '\\s*\\+\\s*(.+)$'));
    if (incMatch) return ind + target + ' += ' + incMatch[1] + ';\n';
    const decMatch = val.match(new RegExp('^' + esc + '\\s*-\\s*(.+)$'));
    if (decMatch) return ind + target + ' -= ' + decMatch[1] + ';\n';
    return ind + target + ' = ' + val + ';\n';
  }

  return ind + modTranspileExpr(text, ctx) + ';\n';
}

function localIsReassigned(name, lines, lineIdx) {
  const pat = new RegExp('^' + name + '\\s*=');
  for (let i = lineIdx + 1; i < lines.length; i++) {
    const text = (lines[i].text || '').trim();
    if (!text) continue;
    if (pat.test(text)) return true;
    if (text.indexOf('? ') !== -1 || text.startsWith('? ') || text.startsWith(': ')) {
      const bare = text.replace(/^[?:]\s*/, '').trim();
      if (pat.test(bare)) return true;
      const parts = bare.split(';').map(function(s) { return s.trim(); }).filter(Boolean);
      for (let p = 0; p < parts.length; p++) {
        if (pat.test(parts[p])) return true;
      }
    }
  }
  return false;
}

function modTranspileValue(expr, ctx) {
  const t = expr.trim();
  const structMatch = t.match(/^\{([\s\S]*)\}$/);
  if (structMatch) return transpileStructLiteral(structMatch[1]);
  return modTranspileExpr(t, ctx);
}

function emitArmBodyV2(lines, typeNames, depth, ctx) {
  let out = ''; const ind = '    '.repeat(depth);
  // Multi-line ternary: condition \n ? true-expr \n : false-expr
  if (lines.length >= 3 && lines[1].text.startsWith('? ') && lines[2].text.startsWith(': ')) {
    const cond = modTranspileExpr(lines[0].text, ctx);
    const trueExpr = lines[1].text.slice(2).trim();
    const falseExpr = lines[2].text.slice(2).trim();
    out += ind + 'if (' + cond + ') {\n';
    out += emitStatementList(trueExpr, ind + '    ');
    out += ind + '} else {\n';
    out += emitStatementList(falseExpr, ind + '    ');
    out += ind + '}\n';
    return out;
  }
  for (let i = 0; i < lines.length; i++) {
    const text = applyOptionalUnwraps(lines[i].text, ctx.narrowedVars || []);
    const am = text.match(/^([^=!<>]+?)\s*=\s*([^=].*)$/);
    if (am) { out += ind + am[1].trim() + ' = ' + modTranspileExpr(am[2].trim(), ctx) + ';\n'; }
    else { out += ind + modTranspileExpr(text, ctx) + ';\n'; }
  }
  return out;
}
function emitForLoopV2(arrExpr, itemVar, indexVar, bodyLines, typeNames, depth, ctx) {
  let out = ''; const ind = '    '.repeat(depth);
  const spec = parseForExprSpec(arrExpr);
  const iterVar = (!spec.reverse && indexVar) ? indexVar : '_i';
  const accessIndexExpr = spec.reverse
    ? buildReverseIndexExpr(spec.startExpr, spec.endExpr, iterVar)
    : iterVar;
  const itemAccessExpr = spec.baseExpr + '[' + accessIndexExpr + ']';

  out += ind + 'var ' + iterVar + ': usize = ' + spec.startExpr + ';\n';
  out += ind + 'while (' + iterVar + ' < ' + spec.endExpr + ') : (' + iterVar + ' += 1) {\n';
  if (spec.reverse && indexVar) {
    out += ind + '    const ' + indexVar + ': usize = ' + accessIndexExpr + ';\n';
  }

  const loopCtx = extendModCtx(ctx, {
    knownVars: addUniqueVars(ctx.knownVars || [], [itemVar].concat(indexVar ? [indexVar] : [])),
    localNames: addUniqueVars(ctx.localNames || [], indexVar ? [indexVar] : []),
    inLoop: true,
  });
  const processedLines = bodyLines.map(function(line) {
    return {
      indent: line.indent,
      text: substituteForLoopVars(line.text, itemVar, itemAccessExpr, indexVar, spec.reverse ? accessIndexExpr : null),
    };
  });
  out += emitModBody(processedLines, 0, typeNames, depth + 1, [], 'void', loopCtx);
  out += ind + '}\n';
  return out;
}
function emitMapFunction(fname, zigParams, zigRet, bodyText, typeNames) {
  const mapMatch = bodyText.match(/return\s+(\w+)\[(\d+)\.\.(\w+)\]\.map\((\w+)\s*=>\s*(\w+)\.(\w+)\)/);
  if (mapMatch) {
    const arr = mapMatch[1]; const end = mapMatch[3]; const field = mapMatch[6];
    let out = 'pub fn ' + fname + '(buf: []i64) []i64 {\n';
    out += '    var i: usize = 0;\n';
    out += '    while (i < ' + end + ') : (i += 1) {\n';
    out += '        buf[i] = ' + arr + '[i].' + field + ';\n';
    out += '    }\n';
    out += '    return buf[0..' + end + '];\n';
    out += '}\n\n';
    return out;
  }
  return 'pub fn ' + fname + '(' + zigParams + ') ' + zigRet + ' {\n    // TODO: map\n}\n\n';
}
function modTranspileForExprV2(expr, baseArr, itemVar, ctx) {
  let e = expr;
  e = e.replace(new RegExp('\\b' + itemVar + '\\.', 'g'), baseArr + '[_i].');
  e = e.replace(new RegExp('\\b' + itemVar + '\\b', 'g'), baseArr + '[_i]');
  return modTranspileExpr(applyOptionalUnwraps(e, (ctx && ctx.narrowedVars) || []), ctx);
}

function parseForExprSpec(expr) {
  let raw = expr.trim();
  let reverse = false;
  if (raw.endsWith('.reverse()')) {
    reverse = true;
    raw = raw.slice(0, -'.reverse()'.length).trim();
  }
  const rangeMatch = raw.match(/^(.+)\[(.+)\.\.(.+)\]$/);
  if (rangeMatch) {
    return {
      baseExpr: rangeMatch[1].trim(),
      startExpr: rangeMatch[2].trim(),
      endExpr: rangeMatch[3].trim(),
      reverse: reverse,
    };
  }
  return {
    baseExpr: raw,
    startExpr: '0',
    endExpr: raw + '.len',
    reverse: reverse,
  };
}

function buildReverseIndexExpr(startExpr, endExpr, iterVar) {
  if (startExpr === '0') return '(' + endExpr + ' - 1 - ' + iterVar + ')';
  return '(' + endExpr + ' - 1 - (' + iterVar + ' - (' + startExpr + ')))';
}

function substituteForLoopVars(text, itemVar, itemAccessExpr, indexVar, indexExpr) {
  let out = text;
  out = out.replace(new RegExp('\\b' + itemVar + '\\.', 'g'), itemAccessExpr + '.');
  out = out.replace(new RegExp('\\b' + itemVar + '\\b', 'g'), itemAccessExpr);
  if (indexVar && indexExpr) {
    out = out.replace(new RegExp('\\b' + indexVar + '\\b', 'g'), indexExpr);
  }
  return out;
}

function extendModCtx(ctx, extra) {
  const next = {
    knownVars: (ctx && ctx.knownVars) ? ctx.knownVars : [],
    localNames: (ctx && ctx.localNames) ? ctx.localNames : [],
    ptrVars: (ctx && ctx.ptrVars) ? ctx.ptrVars.slice() : [],
    narrowedVars: (ctx && ctx.narrowedVars) ? ctx.narrowedVars.slice() : [],
    inLoop: !!(ctx && ctx.inLoop),
  };
  if (!extra) return next;
  if (extra.knownVars) next.knownVars = extra.knownVars;
  if (extra.localNames) next.localNames = extra.localNames;
  if (extra.ptrVars) next.ptrVars = extra.ptrVars;
  if (extra.narrowedVars) next.narrowedVars = extra.narrowedVars;
  if (Object.prototype.hasOwnProperty.call(extra, 'inLoop')) next.inLoop = extra.inLoop;
  return next;
}

function extractNullGuardedVars(cond) {
  return extractNullComparedVars(cond, '==');
}

function extractNonNullVars(cond) {
  if (/\bor\b|\|\|/.test(cond)) return [];
  return extractNullComparedVars(cond, '!=');
}

function extractNullComparedVars(cond, op) {
  const vars = [];
  const re = new RegExp('([A-Za-z_]\\w*(?:\\.[A-Za-z_]\\w*)*)\\s*' + op.replace(/[!=]/g, '\\$&') + '\\s*null', 'g');
  let m;
  while ((m = re.exec(cond)) !== null) {
    if (m[1]) vars.push(m[1]);
  }
  return addUniqueVars([], vars);
}

function addUniqueVars(base, extra) {
  const out = (base || []).slice();
  for (let i = 0; i < (extra || []).length; i++) {
    if (out.indexOf(extra[i]) === -1) out.push(extra[i]);
  }
  return out;
}

function isEarlyExitBranch(expr) {
  const parts = expr.split(';').map(function(s) { return s.trim(); }).filter(Boolean);
  if (parts.length === 0) return false;
  for (let i = 0; i < parts.length; i++) {
    if (!(parts[i].startsWith('return ') || parts[i] === 'return' || parts[i] === 'stop' || parts[i] === 'continue')) return false;
  }
  return true;
}

function applyOptionalUnwraps(text, vars) {
  let out = text;
  const list = (vars || []).slice().sort(function(a, b) { return b.length - a.length; });
  for (let i = 0; i < list.length; i++) {
    out = replaceVarDotAccessWithUnwrap(out, list[i]);
    out = replaceExactVarWithUnwrap(out, list[i]);
    out = out.replace(new RegExp(escapeRegExp(list[i]) + '\\.\\?\\.\\?', 'g'), list[i] + '.?');
    out = out.replace(new RegExp(escapeRegExp(list[i]) + '\\.\\?\\s*=\\s*null', 'g'), list[i] + ' = null');
  }
  return out.replace(/\.\?\.\?/g, '.?');
}

function replaceVarDotAccessWithUnwrap(text, name) {
  const needle = name + '.';
  const replacement = name + '.?.';
  let out = '';
  for (let i = 0; i < text.length;) {
    if (text.slice(i, i + replacement.length) === replacement) {
      out += replacement;
      i += replacement.length;
      continue;
    }
    if (text.slice(i, i + needle.length) === needle) {
      const before = i === 0 ? '' : text[i - 1];
      const beforeOk = !before || !isModIdentChar(before);
      if (beforeOk) {
        out += replacement;
        i += needle.length;
        continue;
      }
    }
    out += text[i];
    i++;
  }
  return out;
}

function replaceExactVarWithUnwrap(text, name) {
  return replaceNeedleWithBoundary(text, name, name + '.?');
}

function replaceNeedleWithBoundary(text, needle, replacement) {
  let out = '';
  for (let i = 0; i < text.length;) {
    if (text.slice(i, i + needle.length) === needle) {
      const before = i === 0 ? '' : text[i - 1];
      const after = i + needle.length >= text.length ? '' : text[i + needle.length];
      const beforeOk = !before || !isModIdentChar(before);
      const afterOk = !after || !isModIdentChar(after);
      if (beforeOk && afterOk) {
        out += replacement;
        i += needle.length;
        continue;
      }
    }
    out += text[i];
    i++;
  }
  return out;
}

function isModIdentChar(ch) {
  return /[A-Za-z0-9_.]/.test(ch);
}

function rewriteKnownFunctionCalls(expr, ctx) {
  const ptrVars = (ctx && ctx.ptrVars) || [];
  let out = '';
  for (let i = 0; i < expr.length;) {
    if (/[A-Za-z_]/.test(expr[i])) {
      let j = i + 1;
      while (j < expr.length && /[A-Za-z0-9_]/.test(expr[j])) j++;
      const name = expr.slice(i, j);
      if (_modFnPtrParams[name] && expr[j] === '(') {
        const end = findMatchingParen(expr, j);
        if (end !== -1) {
          const args = splitCallArgs(expr.slice(j + 1, end)).map(function(arg, idx) {
            const rewritten = rewriteKnownFunctionCalls(arg.trim(), ctx);
            if (_modFnPtrParams[name][idx] && !argIsPointerLike(rewritten, ptrVars)) return '&' + rewritten;
            return rewritten;
          });
          out += name + '(' + args.join(', ') + ')';
          i = end + 1;
          continue;
        }
      }
    }
    out += expr[i];
    i++;
  }
  return out;
}

function findMatchingParen(text, openIdx) {
  let depth = 0;
  let quote = null;
  for (let i = openIdx; i < text.length; i++) {
    const ch = text[i];
    if (quote) {
      if (ch === quote && text[i - 1] !== '\\') quote = null;
      continue;
    }
    if (ch === '"' || ch === "'") {
      quote = ch;
      continue;
    }
    if (ch === '(') depth++;
    if (ch === ')') {
      depth--;
      if (depth === 0) return i;
    }
  }
  return -1;
}

function splitCallArgs(text) {
  const args = [];
  let cur = '';
  let paren = 0;
  let brace = 0;
  let bracket = 0;
  let quote = null;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    if (quote) {
      cur += ch;
      if (ch === quote && text[i - 1] !== '\\') quote = null;
      continue;
    }
    if (ch === '"' || ch === "'") {
      quote = ch;
      cur += ch;
      continue;
    }
    if (ch === '(') paren++;
    if (ch === ')') paren--;
    if (ch === '{') brace++;
    if (ch === '}') brace--;
    if (ch === '[') bracket++;
    if (ch === ']') bracket--;
    if (ch === ',' && paren === 0 && brace === 0 && bracket === 0) {
      args.push(cur);
      cur = '';
      continue;
    }
    cur += ch;
  }
  if (cur.trim() || text.trim() === '') args.push(cur);
  return args.length === 1 && args[0] === '' ? [] : args;
}

function argIsPointerLike(arg, ptrVars) {
  const trimmed = arg.trim();
  if (!trimmed) return false;
  if (trimmed.startsWith('&')) return true;
  if (ptrVars.indexOf(trimmed) !== -1) return true;
  return false;
}

function escapeRegExp(text) {
  return text.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function rewriteExpressionTernary(expr, ctx) {
  const parts = splitTopLevelTernary(expr);
  if (!parts) return null;
  return 'if (' + modTranspileExpr(parts.cond, ctx) + ') ' + modTranspileExpr(parts.thenExpr, ctx) + ' else ' + modTranspileExpr(parts.elseExpr, ctx);
}

function splitTopLevelTernary(expr) {
  let paren = 0;
  let brace = 0;
  let bracket = 0;
  let quote = null;
  let qIndex = -1;
  let nestedTernary = 0;
  for (let i = 0; i < expr.length; i++) {
    const ch = expr[i];
    if (quote) {
      if (ch === quote && expr[i - 1] !== '\\') quote = null;
      continue;
    }
    if (ch === '"' || ch === "'") { quote = ch; continue; }
    if (ch === '(') { paren++; continue; }
    if (ch === ')') { paren--; continue; }
    if (ch === '{') { brace++; continue; }
    if (ch === '}') { brace--; continue; }
    if (ch === '[') { bracket++; continue; }
    if (ch === ']') { bracket--; continue; }
    if (paren !== 0 || brace !== 0 || bracket !== 0) continue;
    if (ch === '?') {
      if (expr[i + 1] === '?') { i++; continue; }
      if (qIndex === -1) qIndex = i;
      else nestedTernary++;
      continue;
    }
    if (ch === ':' && qIndex !== -1) {
      if (nestedTernary > 0) {
        nestedTernary--;
        continue;
      }
      return {
        cond: expr.slice(0, qIndex).trim(),
        thenExpr: expr.slice(qIndex + 1, i).trim(),
        elseExpr: expr.slice(i + 1).trim(),
      };
    }
  }
  return null;
}
