// Emit preamble/imports

function emitPreamble(meta) {
  let out = `//! Generated by tsz compiler (Zig) \u2014 do not edit\n//!\n//! Source: ${meta.basename}\n//! Lane: ${meta.pfLane}\n\n`;
  out += `const std = @import("std");\n`;
  if (meta.hasRuntimeLog) out += `const smith_log = @import("${meta.prefix}log.zig");\n`;
  if (meta.fastBuild) {
    out += `const api = @import("${meta.prefix}api.zig");\n`;
    out += `const layout = api;\n`;
    out += `const Node = api.Node;\nconst Style = api.Style;\nconst Color = api.Color;\n`;
    if (meta.hasState || meta.hasDynamicOA) out += `const state = api.state;\n`;
    out += `const engine = api.engine;\n`;
    if (meta.hasScriptRuntime) out += `const qjs_runtime = api.qjs_runtime;\n`;
  } else {
    out += `const build_options = @import("build_options");\n`;
    out += `const IS_LIB = if (@hasDecl(build_options, "is_lib")) build_options.is_lib else false;\n\n`;
    out += `const layout = @import("${meta.prefix}layout.zig");\n`;
    out += `const Node = layout.Node;\nconst Style = layout.Style;\nconst Color = layout.Color;\n`;
    if (meta.hasState || meta.hasDynamicOA) out += `const state = @import("${meta.prefix}state.zig");\n`;
    out += `const engine = if (IS_LIB) struct {} else @import("${meta.prefix}engine.zig");\n`;
    if (meta.hasScriptRuntime) {
      out += `const qjs_runtime = if (IS_LIB) struct {\n`;
      out += `    pub fn callGlobal(_: []const u8) void {}\n`;
      out += `    pub fn callGlobalStr(_: []const u8, _: []const u8) void {}\n`;
      out += `    pub fn callGlobalInt(_: []const u8, _: i64) void {}\n`;
      out += `    pub fn registerHostFn(_: []const u8, _: ?*const anyopaque, _: u8) void {}\n`;
      out += `    pub fn evalExpr(_: []const u8) void {}\n`;
      out += `} else @import("${meta.prefix}qjs_runtime.zig");\n`;
    }
  }
  // Ensure core.zig export symbols (rjit_state_*) are in the link unit for monolithic builds
  if (!meta.fastBuild) {
    out += `comptime { _ = @import("${meta.prefix}core.zig"); }\n`;
  }
  out += `\n`;
  return out;
}
