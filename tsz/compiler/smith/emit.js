        out += `        _map_count_${imi}[_i] = @min(_oa${im.oaIdx}_len, MAX_MAP_${imi});\n`;
        out += `        {\n        var _j: usize = 0;\n        while (_j < _map_count_${imi}[_i]) : (_j += 1) {\n`;

