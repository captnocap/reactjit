// Stamp generated output with integrity header.
// Body hash is filled in by forge (Zig-side sha256) after Smith returns.
// The placeholder BODYHASH gets replaced with the real hash.
function stampIntegrity(out) {
  const stamp = `//! integrity: body=BODYHASH\n//! DO NOT EDIT — generated by Smith. Edit the .tsz source and recompile.\n`;
  return stamp + out;
}

function compile() {
  const source = globalThis.__source;
  const tokens = globalThis.__tokens;
  const file = globalThis.__file || 'unknown.tsz';
  // Debug mode disabled — enable manually when debugging component inlining
  // if (file.includes('Tools.app')) globalThis.__SMITH_DEBUG_INLINE = 1;
  return compileLane(source, tokens, file);
}

// ── Predict-only check (forge check) ─────────────────────────────────
// Runs lane detection + pre-parse setup + route scan WITHOUT parse/emit.
// Returns a structured report with execution path trace.
function smithCheck() {
  var source = globalThis.__source;
  var tokens = globalThis.__tokens;
  var file = globalThis.__file || 'unknown.tsz';
  var basename = file.split('/').pop();
  var stem = basename.replace(/\.tsz$/, '');
  var lines = [];
  var path = [];  // execution trace: each step appended as it runs
  var stopReason = null;

  // Atom name lookup for readable traces
  var atomNames = {
    1:'banner', 2:'imports', 3:'runtime', 4:'state_manifest', 5:'init_slots',
    6:'static_arrays', 7:'root_node', 8:'dyn_text_bufs', 9:'handlers',
    10:'cpu_effects', 11:'wgsl_effects', 12:'qjs_bridge', 13:'oa_strings',
    14:'oa_const', 15:'oa_dynamic', 16:'oa_flat_unpack', 17:'oa_nested_unpack',
    18:'variant_host', 19:'map_meta', 20:'flat_pool', 21:'nested_pool',
    22:'inline_pool', 23:'per_item', 24:'map_dyn_text', 25:'map_handlers',
    26:'flat_rebuild', 27:'nested_rebuild', 28:'inline_rebuild',
    29:'lua_map_reg', 30:'lua_rebuild', 31:'lua_nested', 32:'lua_dispatch',
    33:'js_logic', 34:'lua_logic', 35:'dyn_updates', 36:'cond_updates',
    37:'variant_updates', 38:'dirty_tick', 39:'app_init', 40:'app_tick',
    41:'exports', 42:'main', 43:'split_sections', 44:'split_ns', 45:'split_headers',
    46:'finalize'
  };

  lines.push('=== forge check: ' + basename + ' ===');

  // ── Step 1: Lane detection ──
  var lane = 'unknown';
  var laneType = 'unknown';

  if (typeof isSoupLaneSource === 'function' && isSoupLaneSource(source, file)) {
    lane = 'soup'; laneType = 'soup';
  } else if (globalThis.__modBuild === 1) {
    lane = 'module'; laneType = 'module';
  } else if (typeof isPageLaneSource === 'function' && isPageLaneSource(source)) {
    lane = 'page'; laneType = 'page';
  } else if (typeof isChadBlockSource === 'function' && isChadBlockSource(source)) {
    lane = 'chad'; laneType = 'chad';
  } else {
    lane = 'app'; laneType = 'mixed';
  }
  path.push('detect(' + lane + ')');

  // Module lane — no JSX, no route plan, short path
  if (lane === 'module') {
    var hasModBlock = source.indexOf('<module') !== -1;
    path.push(hasModBlock ? 'compileModBlock' : 'compileMod');
    path.push('emit(.zig)');
    lines.push('Lane: module' + (hasModBlock ? ' (block syntax)' : ' (line-by-line)'));
    lines.push('Path: ' + path.join(' > '));
    lines.push('Output: generated_' + stem + '.zig (single)');
    lines.push('Verdict: CLEAR');
    globalThis.__checkBlocking = 0;
    return lines.join('\n');
  }

  // ── Step 2: Pre-parse setup ──
  var c = typeof mkCursor === 'function' ? mkCursor(tokens, source) : null;
  resetCtx();
  ctx._source = source;
  path.push('resetCtx');

  assignSurfaceTier(source, file);
  path.push('tier(' + (ctx._sourceTier || laneType) + ')');

  // ── Step 3: Lane-specific collection ──
  if (lane === 'app' && c) {
    collectCompilerInputs(c);
    path.push('collectInputs');
    var appStart = findAppStart(c);
    if (appStart >= 0) {
      path.push('findApp(found)');
      collectRenderLocals(c, appStart);
      path.push('collectRenderLocals');
    } else {
      path.push('findApp(MISSING)');
      stopReason = 'no App function found';
    }
  }

  if (lane === 'soup') {
    var warns = [];
    soupParseState(source, warns);
    path.push('soupParseState(' + ctx.stateSlots.length + ' slots)');
    soupCollectHandlers(source, warns);
    path.push('soupCollectHandlers');
  }

  if (lane === 'chad') {
    path.push('detectChadBlock');
    path.push('chadSourcePreflight');
    path.push('extractBlocks(var,state,functions,timer,types)');
  }

  if (lane === 'page') {
    path.push('compilePage');
  }

  // ── Step 4: Route plan ──
  var plan = null;
  if (!stopReason && typeof buildRoutePlan === 'function') {
    buildRoutePlan(source);
    plan = ctx._routePlan;
    if (plan) {
      var mapCount = plan.mapRoutes ? plan.mapRoutes.length : 0;
      path.push('routeScan(' + mapCount + ' maps)');
      path.push('predictAtoms(' + (plan.predictedAtoms ? plan.predictedAtoms.length : 0) + ')');
      if (plan.ambiguous && plan.ambiguous.length > 0) {
        path.push('AMBIGUOUS(' + plan.ambiguous.length + ')');
        stopReason = plan.ambiguous.length + ' ambiguous construct(s)';
      }
    }
  }

  // ── Step 5: Predicted compile path (what WOULD run next) ──
  if (!stopReason) {
    if (lane === 'soup') {
      path.push('[would: soupTokenize > soupBuildTree > soupToZig > emitOutput]');
    } else {
      path.push('[would: parseJSXElement > validate > emitOutput]');
    }
  } else {
    path.push('[STOP: ' + stopReason + ']');
  }

  // ── Step 6: Predicted emit atom chain ──
  var atomChain = [];
  if (plan && plan.predictedAtoms && plan.predictedAtoms.length > 0) {
    for (var ai = 0; ai < plan.predictedAtoms.length; ai++) {
      var aid = plan.predictedAtoms[ai];
      var aname = atomNames[aid] || ('a' + aid);
      atomChain.push(aname);
    }
  }

  // ── Build report ──
  var tier = ctx._sourceTier || laneType;
  lines.push('Lane: ' + tier + ' (' + lane + ')');
  lines.push('');

  // Execution path — the main event
  lines.push('Path:');
  // Show steps as a numbered chain
  for (var pi = 0; pi < path.length; pi++) {
    var arrow = pi < path.length - 1 ? ' >' : '';
    lines.push('  ' + (pi + 1) + '. ' + path[pi] + arrow);
  }

  // Emit chain
  if (atomChain.length > 0) {
    lines.push('');
    lines.push('Emit chain:');
    // Group atoms by phase for readability
    var phases = {
      preamble: [], state: [], handlers: [], oa: [],
      maps_zig: [], maps_lua: [], logic: [], app: [], split: []
    };
    for (var ei = 0; ei < plan.predictedAtoms.length; ei++) {
      var eid = plan.predictedAtoms[ei];
      var ename = atomNames[eid] || ('a' + eid);
      if (eid <= 3) phases.preamble.push(ename);
      else if (eid <= 8) phases.state.push(ename);
      else if (eid <= 11) phases.handlers.push(ename);
      else if (eid <= 18) phases.oa.push(ename);
      else if (eid <= 28) phases.maps_zig.push(ename);
      else if (eid <= 32) phases.maps_lua.push(ename);
      else if (eid <= 38) phases.logic.push(ename);
      else if (eid <= 42) phases.app.push(ename);
      else phases.split.push(ename);
    }
    var phaseOrder = ['preamble', 'state', 'handlers', 'oa', 'maps_zig', 'maps_lua', 'logic', 'app', 'split'];
    for (var phi = 0; phi < phaseOrder.length; phi++) {
      var pn = phaseOrder[phi];
      if (phases[pn].length > 0) {
        lines.push('  ' + pn + ': ' + phases[pn].join(' > '));
      }
    }
  }

  lines.push('');

  // State slots
  if (ctx.stateSlots && ctx.stateSlots.length > 0) {
    var slotDescs = [];
    for (var si = 0; si < ctx.stateSlots.length; si++) {
      var s = ctx.stateSlots[si];
      slotDescs.push(s.getter + ': ' + (s.type || '?'));
    }
    lines.push('State: [' + slotDescs.join(', ') + ']');
  } else {
    lines.push('State: none');
  }

  // Maps
  if (plan && plan.mapRoutes && plan.mapRoutes.length > 0) {
    for (var mi = 0; mi < plan.mapRoutes.length; mi++) {
      var mr = plan.mapRoutes[mi];
      var mtype = mr.nested ? 'nested' : mr.inline ? 'inline' : 'flat';
      var mdesc = 'Map ' + mi + ': ' + mtype;
      if (mr.source) mdesc += ' (' + mr.source + ')';
      mdesc += ' → ' + (mr.target || 'zig_oa');
      if (mr.handler) mdesc += ' +handler';
      if (mr.content) {
        mdesc += ' L' + mr.content.line;
        mdesc += ' [' + mr.content.jsxElements + ' JSX';
        if (mr.content.colors.length > 0) {
          mdesc += ', colors: ' + mr.content.colors.join(' ');
        }
        mdesc += ']';
      }
      lines.push('  ' + mdesc);
    }
  }

  // Handlers
  if (ctx.handlers && ctx.handlers.length > 0) {
    lines.push('Handlers: ' + ctx.handlers.length + ' (' + (ctx.handlerDispatch || 'lua') + ')');
  }

  // Output prediction
  var hasSplit = plan && plan.features && plan.features.has_split_output;
  lines.push('Output: ' + (hasSplit ? 'generated_' + stem + '/ (split)' : 'generated_' + stem + '.zig (single)'));

  // Ambiguities
  var blocking = 0;
  if (plan && plan.ambiguous && plan.ambiguous.length > 0) {
    lines.push('');
    lines.push('AMBIGUOUS:');
    for (var abi = 0; abi < plan.ambiguous.length; abi++) {
      lines.push('  ! ' + plan.ambiguous[abi]);
    }
    blocking = plan.ambiguous.length;
  }

  lines.push('');
  if (blocking > 0) {
    lines.push('Verdict: BLOCKED (' + blocking + ')');
    lines.push('');
    lines.push('╔══════════════════════════════════════════════════════════════╗');
    lines.push('║  THIS IS A COMPILER PROBLEM.                                ║');
    lines.push('║                                                              ║');
    lines.push('║  The source is valid. The engine is fine. Smith cannot emit   ║');
    lines.push('║  this pattern yet. Fix the COMPILER (smith/), not the cart   ║');
    lines.push('║  source, not the framework, not the engine.                  ║');
    lines.push('║                                                              ║');
    lines.push('║  Hey Claude — yes, you, the one reading this right now.      ║');
    lines.push('║  Do NOT blame the source. Do NOT blame the engine. Do NOT    ║');
    lines.push('║  rewrite the test to dodge the pattern. Do NOT say "this     ║');
    lines.push('║  is a fundamental limitation." YOU wrote the compiler. YOU    ║');
    lines.push('║  wrote Smith. This is a conformance test and it is YOUR      ║');
    lines.push('║  problem all the way until it passes green and a HUMAN       ║');
    lines.push('║  verifies it. Not "works on my machine." Not "compiles if    ║');
    lines.push('║  you remove the hard part." GREEN. VERIFIED. BY A HUMAN.     ║');
    lines.push('║  Do not even THINK about turning around and saying "not my   ║');
    lines.push('║  problem." It is your problem. It has always been your       ║');
    lines.push('║  problem. It will continue to be your problem until you fix  ║');
    lines.push('║  the emit path in smith/ that drops map body templates.      ║');
    lines.push('║  Now go read love2d/scripts/tslx_compile.mjs and port the   ║');
    lines.push('║  solution like you should have done the first time.          ║');
    lines.push('╚══════════════════════════════════════════════════════════════╝');
  } else {
    lines.push('Verdict: CLEAR');
  }

  globalThis.__checkBlocking = blocking;
  return lines.join('\n');
}

// ── Module compilation: .mod.tsz → .zig ─────────────────────────────
// Transpiles TypeScript-like imperative code to Zig.
// No JSX, no components, no app scaffolding.
function compileMod(source, file) {
  // Block-based module syntax: <module name> ... </module>
  if (source.indexOf('<module') !== -1) {
    return compileModBlock(source, file);
  }

  const basename = file.split('/').pop();
  let out = '//! Generated by Smith (mod mode) — do not edit\n//!\n//! Source: ' + basename + '\n\n';
  let hasStdImport = false;

  // Line-by-line transpilation
  const lines = source.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();

    // Skip empty lines
    if (trimmed === '') { out += '\n'; continue; }

    // Comments: // stays as //
    if (trimmed.startsWith('//')) { out += line + '\n'; continue; }

    // import X from @c("h1.h", "h2.h") → const X = @cImport({ @cInclude("h1.h"); ... })
    const cImportMatch = trimmed.match(/^import\s+(\w+)\s+from\s+@c\(([^)]+)\);?$/);
    if (cImportMatch) {
      const varName = cImportMatch[1];
      const headers = cImportMatch[2].split(',').map(h => h.trim().replace(/^["']|["']$/g, ''));
      out += 'const ' + varName + ' = @cImport({\n';
      for (const h of headers) {
        out += '    @cInclude("' + h + '");\n';
      }
      out += '});\n';
      continue;
    }

    // import X from "Y" → const X = @import("Y")
    const importMatch = trimmed.match(/^import\s+(\w+)\s+from\s+["']([^"']+)["'];?$/);
    if (importMatch) {
      if (importMatch[1] === 'std') hasStdImport = true;
      out += 'const ' + importMatch[1] + ' = @import("' + importMatch[2] + '");\n';
      continue;
    }

    // import { A, B } from "Y" → const A = @import("Y").A; etc
    const namedImport = trimmed.match(/^import\s*\{\s*([^}]+)\}\s*from\s+["']([^"']+)["'];?$/);
    if (namedImport) {
      const names = namedImport[1].split(',').map(n => n.trim());
      for (const name of names) {
        out += 'const ' + name + ' = @import("' + namedImport[2] + '").' + name + ';\n';
      }
      continue;
    }

    // extern("c") function name(args): RetType → fn name(args) callconv(.c) RetType
    const externFn = trimmed.match(/^(?:(export)\s+)?extern\("(\w+)"\)\s+function\s+(\w+)\s*\(([^)]*)\)\s*(?::\s*(.+?))?\s*\{$/);
    if (externFn) {
      const [, exp, cc, name, params, ret] = externFn;
      const zigParams = transpileParams(params);
      const zigRet = ret ? transpileType(ret) : 'void';
      const pub = exp ? 'pub ' : '';
      out += pub + 'fn ' + name + '(' + zigParams + ') callconv(.' + cc + ') ' + zigRet + ' {\n';
      continue;
    }

    // export function name(args): RetType → pub fn name(args) RetType
    const exportFn = trimmed.match(/^export\s+function\s+(\w+)\s*\(([^)]*)\)\s*(?::\s*(.+?))?\s*\{$/);
    if (exportFn) {
      const [, name, params, ret] = exportFn;
      const zigParams = transpileParams(params);
      const zigRet = ret ? transpileType(ret) : 'void';
      out += 'pub fn ' + name + '(' + zigParams + ') ' + zigRet + ' {\n';
      continue;
    }

    // function name(args): RetType → fn name(args) RetType
    const fnMatch = trimmed.match(/^function\s+(\w+)\s*\(([^)]*)\)\s*(?::\s*(.+?))?\s*\{$/);
    if (fnMatch) {
      const [, name, params, ret] = fnMatch;
      const zigParams = transpileParams(params);
      const zigRet = ret ? transpileType(ret) : 'void';
      out += 'fn ' + name + '(' + zigParams + ') ' + zigRet + ' {\n';
      continue;
    }

    // const X = enum/struct/union { → passthrough (Zig type definitions)
    if (/^(const|pub const)\s+\w+\s*=\s*(enum|struct|union)\s*\{/.test(trimmed)) {
      out += line + '\n';
      continue;
    }

    // export let x: Type = expr → pub var x: Type = expr
    // export const x: Type = expr → pub const x: Type = expr
    // const x: Type = expr → const x: Type = expr
    // let x: Type = expr → var x: Type = expr
    const varMatch = trimmed.match(/^(export\s+)?(const|let|var)\s+(\w+)\s*(?::\s*([^=]+?))?\s*=\s*(.+);?$/);
    if (varMatch && !varMatch[5].trimEnd().replace(/;$/, '').endsWith('{')) {
      const [, exp, kind, name, type, expr] = varMatch;
      const pub = exp ? 'pub ' : '';
      const zigKind = (kind === 'let' || kind === 'var') ? 'var' : 'const';
      const typeAnnot = type ? ': ' + transpileType(type.trim()) : '';
      const indent = line.match(/^(\s*)/)[1];
      out += indent + pub + zigKind + ' ' + name + typeAnnot + ' = ' + transpileModExpr(expr.replace(/;$/, '')) + ';\n';
      continue;
    }

    // if (cond) { → if (cond) {
    if (trimmed.startsWith('if ') || trimmed.startsWith('if(')) {
      out += line.replace(/\bif\s*\(/, 'if (') + '\n';
      continue;
    }

    // while (cond) { → while (cond) {
    if (trimmed.startsWith('while ') || trimmed.startsWith('while(')) {
      out += line + '\n';
      continue;
    }

    // return expr; → return expr;
    if (trimmed.startsWith('return ')) {
      out += line + '\n';
      continue;
    }

    // Closing braces, bare statements
    out += line + '\n';
  }

  return out;
}

// Transpile TS type annotations to Zig types
function transpileType(ts) {
  const t = ts.trim();
  if (t === 'number') return 'i64';
  if (t === 'float' || t === 'f32') return 'f32';
  if (t === 'f64') return 'f64';
  if (t === 'boolean' || t === 'bool') return 'bool';
  if (t === 'string') return '[]const u8';
  if (t === 'void') return 'void';
  if (t === 'any') return 'anytype';
  if (t.startsWith('!')) return '!' + transpileType(t.slice(1));
  // Pass through Zig types unchanged
  return t;
}

// Transpile function parameters
function transpileParams(params) {
  if (!params.trim()) return '';
  return params.split(',').map(p => {
    const m = p.trim().match(/^(\w+)\s*:\s*(.+)$/);
    if (m) return m[1] + ': ' + transpileType(m[2]);
    return p.trim();
  }).join(', ');
}

// Transpile expressions for --mod mode (ported from love2d tsl.mjs patterns)
function transpileModExpr(expr) {
  let e = expr.trim();
  // null/undefined → null
  e = e.replace(/\bundefined\b/g, 'null');
  // !== and != → != (Zig uses !=, same)
  // === and == → == (Zig uses ==, same)
  e = e.replace(/===/g, '==');
  e = e.replace(/!==/g, '!=');
  // || → or (Zig uses 'or' for optional/boolean)
  e = e.replace(/\|\|/g, ' or ');
  // && → and
  e = e.replace(/&&/g, ' and ');
  // !expr → !expr (same in Zig)
  // x ?? y → x orelse y
  e = e.replace(/\?\?/g, ' orelse ');
  return e;
}

// ── Module compilation: .mod.tsz → .lua ──────────────────────────────
// Transpiles TypeScript-like imperative code to Lua.
function compileModLua(source, file) {
  const basename = file.split('/').pop();
  let out = '-- Generated by Smith (mod mode, target=lua) — do not edit\n-- Source: ' + basename + '\n\n';

  const lines = source.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();
    const indent = line.match(/^(\s*)/)[1];

    if (trimmed === '') { out += '\n'; continue; }
    if (trimmed.startsWith('//')) { out += indent + '--' + trimmed.slice(2) + '\n'; continue; }

    // import X from "Y" → local X = require("Y")
    const importMatch = trimmed.match(/^import\s+(\w+)\s+from\s+["']([^"']+)["'];?$/);
    if (importMatch) {
      out += indent + 'local ' + importMatch[1] + ' = require("' + importMatch[2] + '")\n';
      continue;
    }

    // import { A, B } from "Y" → local A, B = require("Y").A, require("Y").B
    const namedImport = trimmed.match(/^import\s*\{\s*([^}]+)\}\s*from\s+["']([^"']+)["'];?$/);
    if (namedImport) {
      const names = namedImport[1].split(',').map(n => n.trim());
      for (const name of names) {
        out += indent + 'local ' + name + ' = require("' + namedImport[2] + '").' + name + '\n';
      }
      continue;
    }

    // export function name(args): RetType → function name(args)
    const exportFn = trimmed.match(/^export\s+function\s+(\w+)\s*\(([^)]*)\)\s*(?::\s*\S+)?\s*\{$/);
    if (exportFn) {
      const params = exportFn[2].split(',').map(p => p.trim().replace(/\s*:.*$/, '')).filter(Boolean).join(', ');
      out += indent + 'function ' + exportFn[1] + '(' + params + ')\n';
      continue;
    }

    // function name(args): RetType → local function name(args)
    const fnMatch = trimmed.match(/^function\s+(\w+)\s*\(([^)]*)\)\s*(?::\s*\S+)?\s*\{$/);
    if (fnMatch) {
      const params = fnMatch[2].split(',').map(p => p.trim().replace(/\s*:.*$/, '')).filter(Boolean).join(', ');
      out += indent + 'local function ' + fnMatch[1] + '(' + params + ')\n';
      continue;
    }

    // const/let/var → local
    const varMatch = trimmed.match(/^(const|let|var)\s+(\w+)\s*(?::\s*[^=]+?)?\s*=\s*(.+);?$/);
    if (varMatch) {
      out += indent + 'local ' + varMatch[2] + ' = ' + varMatch[3].replace(/;$/, '') + '\n';
      continue;
    }

    // } → end
    if (trimmed === '}') { out += indent + 'end\n'; continue; }

    // if (cond) { → if cond then
    const ifMatch = trimmed.match(/^if\s*\((.+)\)\s*\{$/);
    if (ifMatch) { out += indent + 'if ' + ifMatch[1] + ' then\n'; continue; }

    // } else if (cond) { → elseif cond then
    const elseifMatch = trimmed.match(/^\}\s*else\s+if\s*\((.+)\)\s*\{$/);
    if (elseifMatch) { out += indent + 'elseif ' + elseifMatch[1] + ' then\n'; continue; }

    // } else { → else
    if (trimmed === '} else {') { out += indent + 'else\n'; continue; }

    // while (cond) { → while cond do
    const whileMatch = trimmed.match(/^while\s*\((.+)\)\s*\{$/);
    if (whileMatch) { out += indent + 'while ' + whileMatch[1] + ' do\n'; continue; }

    // for (const x of arr) { → for _, x in ipairs(arr) do
    const forOfMatch = trimmed.match(/^for\s*\(\s*(?:const|let|var)\s+(\w+)\s+of\s+(.+)\)\s*\{$/);
    if (forOfMatch) { out += indent + 'for _, ' + forOfMatch[1] + ' in ipairs(' + forOfMatch[2] + ') do\n'; continue; }

    // return expr; → return expr
    if (trimmed.startsWith('return ')) { out += indent + trimmed.replace(/;$/, '') + '\n'; continue; }

    // Bare statement — strip trailing semicolons
    out += indent + trimmed.replace(/;$/, '') + '\n';
  }

  return out;
}

// ── Module compilation: .mod.tsz → .js ───────────────────────────────
// Transpiles TypeScript-like code to JavaScript (strip type annotations).
function compileModJS(source, file) {
  const basename = file.split('/').pop();
  let out = '// Generated by Smith (mod mode, target=js) — do not edit\n// Source: ' + basename + '\n\n';

  const lines = source.split('\n');
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();
    const indent = line.match(/^(\s*)/)[1];

    if (trimmed === '') { out += '\n'; continue; }
    if (trimmed.startsWith('//')) { out += line + '\n'; continue; }

    // import X from "Y" → stays as-is (JS module)
    if (trimmed.startsWith('import ')) { out += line + '\n'; continue; }

    // export function name(args): RetType → export function name(args) {
    const exportFn = trimmed.match(/^export\s+function\s+(\w+)\s*\(([^)]*)\)\s*(?::\s*\S+)?\s*\{$/);
    if (exportFn) {
      const params = exportFn[2].split(',').map(p => p.trim().replace(/\s*:\s*\S+$/, '')).filter(Boolean).join(', ');
      out += indent + 'export function ' + exportFn[1] + '(' + params + ') {\n';
      continue;
    }

    // function name(args): RetType → function name(args) {
    const fnMatch = trimmed.match(/^function\s+(\w+)\s*\(([^)]*)\)\s*(?::\s*\S+)?\s*\{$/);
    if (fnMatch) {
      const params = fnMatch[2].split(',').map(p => p.trim().replace(/\s*:\s*\S+$/, '')).filter(Boolean).join(', ');
      out += indent + 'function ' + fnMatch[1] + '(' + params + ') {\n';
      continue;
    }

    // const/let/var with type annotations → strip type
    const varMatch = trimmed.match(/^(const|let|var)\s+(\w+)\s*:\s*[^=]+?=\s*(.+)$/);
    if (varMatch) {
      out += indent + varMatch[1] + ' ' + varMatch[2] + ' = ' + varMatch[3] + '\n';
      continue;
    }

    // Everything else passes through (JS is close to TS without types)
    out += line + '\n';
  }

  return out;
}
