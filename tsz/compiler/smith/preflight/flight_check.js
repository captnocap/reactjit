// ── Flight Check ────────────────────────────────────────────────
//
// Post-emit verifier. Reads ctx._routePlan and the generated Zig
// output string. Verifies that what the route scanner predicted
// matches what emit actually produced.
//
// This is a COMPILER SELF-TEST, not user validation. If flight
// check fails, the compiler has a bug — the plan said X, the
// output says Y. User-facing errors stay in preflight.
//
// Returns { ok, mismatches[] }. Each mismatch is a diagnostic
// string describing what was expected vs what was found.

function flightCheck(ctx, zigOutput) {
  var plan = ctx._routePlan;
  if (!plan) return { ok: true, mismatches: [] };

  var mismatches = [];

  // ── Map count verification ──
  var f = plan.features;

  if (f.has_maps) {
    // Count _rebuildMapN functions in output (Zig OA maps)
    var zigMapRebuilds = 0;
    var rebuildRe = /fn _rebuildMap\d+/g;
    var match;
    while ((match = rebuildRe.exec(zigOutput)) !== null) zigMapRebuilds++;

    // Count __rebuildLuaMapN patterns (Lua maps)
    var luaMapRebuilds = 0;
    var luaRebuildRe = /__rebuildLuaMap\d+/g;
    while ((match = luaRebuildRe.exec(zigOutput)) !== null) luaMapRebuilds++;

    // Verify map route predictions
    var plannedZigMaps = 0;
    var plannedLuaMaps = 0;
    for (var ri = 0; ri < plan.mapRoutes.length; ri++) {
      if (plan.mapRoutes[ri].target === 'zig_oa') plannedZigMaps++;
      else plannedLuaMaps++;
    }

    // Only flag if we predicted maps but got zero — some maps may be
    // reclassified during parse (e.g. computed expr → lua), so exact
    // counts won't always match. The real invariant is: if plan says
    // "maps exist", output must have map infrastructure.
    if (plannedZigMaps > 0 && zigMapRebuilds === 0 && luaMapRebuilds === 0) {
      mismatches.push(
        'ROUTE: planned ' + plannedZigMaps + ' zig maps but output has 0 rebuild functions'
      );
    }
  }

  // ── State manifest verification ──
  if (f.has_state) {
    if (zigOutput.indexOf('state_slots') < 0 && zigOutput.indexOf('getSlot') < 0) {
      mismatches.push('ROUTE: planned state slots but output has no state infrastructure');
    }
  }

  // ── Handler verification ──
  if (f.has_handlers) {
    // At minimum, handler dispatch table or handler fn pointers should exist
    if (zigOutput.indexOf('handler') < 0 && zigOutput.indexOf('Handler') < 0 &&
        zigOutput.indexOf('dispatch') < 0) {
      mismatches.push('ROUTE: planned handlers but output has no handler infrastructure');
    }
  }

  // ── Object array verification ──
  if (f.has_object_arrays) {
    if (zigOutput.indexOf('qjs') < 0 && zigOutput.indexOf('QuickJS') < 0 &&
        zigOutput.indexOf('object_array') < 0 && zigOutput.indexOf('objectArray') < 0 &&
        zigOutput.indexOf('oa_') < 0) {
      mismatches.push('ROUTE: planned object arrays but output has no OA infrastructure');
    }
  }

  // ── Dynamic text verification ──
  if (f.has_dyn_texts) {
    if (zigOutput.indexOf('dyn_text') < 0 && zigOutput.indexOf('dynText') < 0 &&
        zigOutput.indexOf('fmt_buf') < 0 && zigOutput.indexOf('bufPrint') < 0 &&
        zigOutput.indexOf('.text =') < 0) {
      // Don't flag — dynamic text might resolve to static during parse.
      // This is advisory only for now.
    }
  }

  // ── Atom coverage verification ──
  // Check that always-on atoms produced output markers
  if (zigOutput.indexOf('Generated by tsz compiler') < 0) {
    mismatches.push('ROUTE: compile banner (a001) missing from output');
  }

  // ── Leaked unknown patterns ──
  if (zigOutput.indexOf('UNKNOWN_PATTERN') >= 0 || zigOutput.indexOf('???') >= 0) {
    mismatches.push('ROUTE: unknown pattern markers leaked into output');
  }

  // ── Plan vs actual atom count (informational) ──
  // Count how many atoms actually fired by checking ctx._emitAtomTrace if available
  if (ctx._emitAtomTrace) {
    var planned = plan.predictedAtoms.length;
    var actual = ctx._emitAtomTrace.length;
    // Allow some variance — parse can discover features the scanner missed
    if (actual > planned + 5) {
      mismatches.push(
        'ROUTE: atom count drift — planned ' + planned + ', actual ' + actual +
        ' (>5 extra atoms fired, scanner may be under-predicting)'
      );
    }
  }

  return {
    ok: mismatches.length === 0,
    mismatches: mismatches,
  };
}
