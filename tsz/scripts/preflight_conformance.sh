#!/bin/bash
# Full preflight conformance verification
# Runs every conformance cart through the REAL build pipeline
# Records: preflight result, zig compilation result, notes
set -o pipefail
cd "$(dirname "$0")/.."

OUTFILE="docs/PREFLIGHT_CONFORMANCE.md"
mkdir -p docs

# Header
cat > "$OUTFILE" << 'HDR'
# Preflight Conformance Results

Generated by `scripts/preflight_conformance.sh` — real builds, not fabricated.

| Cart | Preflight | Zig Build | Notes |
|------|-----------|-----------|-------|
HDR

TOTAL=0
PF_PASS=0
PF_BLOCK=0
ZIG_PASS=0
ZIG_FAIL=0
SKIP=0

for f in carts/conformance/*.tsz; do
    NAME=$(basename "$f" .tsz)
    GEN="generated_${NAME}.zig"
    TOTAL=$((TOTAL+1))

    # Classify cart type
    CART_TYPE="app"
    case "$NAME" in
        *.script) CART_TYPE="script-lib" ;;
        *.cls)    CART_TYPE="cls-style" ;;
        *.app)    CART_TYPE="app" ;;
        s*c_*)    CART_TYPE="surface-c" ;;  # s01c, s02c etc — chad-tier surface
        s*b_*)    CART_TYPE="surface-b" ;;
        s*a_*)    CART_TYPE="surface-a" ;;
    esac

    # Clean stale output to avoid false reads
    rm -f "$GEN"

    # Step 1: Forge (smith compile) — use --single for monolithic output
    FORGE_OUT=$(./zig-out/bin/forge build --single "$f" 2>&1)
    FORGE_RC=$?
    if [ $FORGE_RC -ne 0 ]; then
        echo "| $NAME | FORGE FAIL | - | forge exit $FORGE_RC |" >> "$OUTFILE"
        echo "FORGE FAIL: $NAME"
        continue
    fi

    # Step 2: Check preflight result
    PF_RESULT="PASS"
    PF_NOTE=""
    if grep -q "PREFLIGHT BLOCKED" "$GEN" 2>/dev/null; then
        PF_RESULT="BLOCKED"
        PF_BLOCK=$((PF_BLOCK+1))
        PF_NOTE=$(grep "FATAL:" "$GEN" 2>/dev/null | head -1 | sed 's/^\/\/! FATAL: //')
        echo "| $NAME | **BLOCKED** | skipped | $PF_NOTE |" >> "$OUTFILE"
        echo "BLOCKED: $NAME — $PF_NOTE"
        continue
    fi
    PF_PASS=$((PF_PASS+1))

    # Step 3: Zig compilation
    # .script.tsz files generate libraries (no main) — they need zig build app-lib, not app
    # .cls.tsz files are style-only — not standalone buildable
    # Surface c-tier carts also generate libraries
    ZIG_RESULT="PASS"
    ZIG_NOTE=""

    case "$CART_TYPE" in
        script-lib)
            # Script carts generate .so libraries — build as app-lib
            ZIG_OUT=$(zig build app-lib -Dapp-name="$NAME" -Dapp-source="$GEN" -Doptimize=ReleaseFast 2>&1)
            ZIG_RC=$?
            ;;
        cls-style)
            # Style files — not buildable standalone, skip zig
            echo "| $NAME | PASS | skipped | style-only (.cls) |" >> "$OUTFILE"
            SKIP=$((SKIP+1))
            echo "SKIP: $NAME (cls)"
            continue
            ;;
        *)
            # Regular app carts — full zig build app
            ZIG_OUT=$(zig build app -Dapp-name="$NAME" -Dapp-source="$GEN" -Doptimize=ReleaseFast 2>&1)
            ZIG_RC=$?
            ;;
    esac

    if [ $ZIG_RC -eq 0 ]; then
        ZIG_PASS=$((ZIG_PASS+1))
        # Verify binary actually exists and has recent timestamp
        BIN="zig-out/bin/$NAME"
        if [ -f "$BIN" ]; then
            BIN_SIZE=$(stat -c%s "$BIN" 2>/dev/null || echo "?")
            ZIG_NOTE="binary=${BIN_SIZE}b"
        elif [ -f "zig-out/lib/lib${NAME}.so" ]; then
            BIN_SIZE=$(stat -c%s "zig-out/lib/lib${NAME}.so" 2>/dev/null || echo "?")
            ZIG_NOTE="lib=${BIN_SIZE}b"
        fi
        echo "| $NAME | PASS | **PASS** | $ZIG_NOTE |" >> "$OUTFILE"
        echo "PASS: $NAME"
    else
        ZIG_FAIL=$((ZIG_FAIL+1))
        ERRMSG=$(echo "$ZIG_OUT" | grep "error:" | head -1 | sed 's/.*error: //' | cut -c1-80)
        echo "| $NAME | PASS | **FAIL** | $ERRMSG |" >> "$OUTFILE"
        echo "FAIL: $NAME — $ERRMSG"
    fi
done

# Summary
cat >> "$OUTFILE" << EOF

## Summary

- **Total carts**: $TOTAL
- **Preflight PASS**: $PF_PASS
- **Preflight BLOCKED**: $PF_BLOCK (true positives — would have failed or produced broken output)
- **Zig Build PASS**: $ZIG_PASS
- **Zig Build FAIL**: $ZIG_FAIL (pre-existing compiler bugs, not preflight issues)
- **Skipped**: $SKIP (style-only files)

Generated: $(date -Iseconds)
EOF

echo
echo "=== SUMMARY ==="
echo "Total: $TOTAL  PF Pass: $PF_PASS  PF Blocked: $PF_BLOCK"
echo "Zig Pass: $ZIG_PASS  Zig Fail: $ZIG_FAIL  Skipped: $SKIP"
