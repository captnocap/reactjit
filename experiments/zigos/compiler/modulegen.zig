//! Module orchestrator for imperative .tsz files.
//!
//! Top-level code generator for .tsz files that have no App function
//! and no JSX — pure imperative TypeScript that compiles to a Zig module.
//!
//! Delegates to:
//!   typegen — enum, interface, type alias declarations
//!   stmtgen — function bodies (control flow, variable declarations)
//!   exprgen — expressions within statements
//!
//! Produces a complete .zig file as output.

const std = @import("std");
const lexer_mod = @import("lexer.zig");
const Lexer = lexer_mod.Lexer;
const Token = lexer_mod.Token;
const TokenKind = lexer_mod.TokenKind;
const typegen = @import("typegen.zig");
const stmtgen = @import("stmtgen.zig");
const exprgen = @import("exprgen.zig");

/// Generate a complete Zig module from an imperative .tsz file.
/// Called instead of codegen.Generator.generate() when the file has
/// no App function and no JSX.
pub fn generate(
    alloc: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
    input_file: []const u8,
) ![]const u8 {
    var out: std.ArrayListUnmanaged(u8) = .{};

    // ── File header ─────────────────────────────────────────────
    const basename = std.fs.path.basename(input_file);
    try out.appendSlice(alloc, "//! ──── GENERATED FILE — DO NOT EDIT ────\n//!\n");
    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
        "//! Source: {s}\n", .{input_file}));
    try out.appendSlice(alloc, "//! Generated by: tsz compile-runtime (imperative mode)\n");
    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
        "//!\n//! To regenerate: tsz compile-runtime {s}\n", .{input_file}));
    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
        "//! To modify: edit {s} and recompile\n", .{basename}));
    try out.appendSlice(alloc, "\nconst std = @import(\"std\");\n");

    // Helper: convert any numeric type to f32 (bridges TS's single `number` type to Zig's int/float split)
    try out.appendSlice(alloc,
        \\
        \\inline fn asF32(val: anytype) f32 {
        \\    return switch (@typeInfo(@TypeOf(val))) {
        \\        .int, .comptime_int => @floatFromInt(val),
        \\        .comptime_float => val,
        \\        .float => if (@TypeOf(val) == f32) val else @floatCast(val),
        \\        .optional => blk: {
        \\            const v = val orelse 0;
        \\            break :blk switch (@typeInfo(@TypeOf(v))) {
        \\                .int, .comptime_int => @as(f32, @floatFromInt(v)),
        \\                .comptime_float => @as(f32, v),
        \\                .float => if (@TypeOf(v) == f32) v else @floatCast(v),
        \\                else => @compileError("asF32: unsupported optional inner type"),
        \\            };
        \\        },
        \\        else => @compileError("asF32: unsupported type"),
        \\    };
        \\}
        \\
    );

    // ── Phase 1: Imports ────────────────────────────────────────
    try emitImports(alloc, lex, source, &out);

    // ── Phase 2: Type declarations ──────────────────────────────
    const type_decls = try typegen.emitTypeDeclarations(alloc, lex, source);
    if (type_decls.len > 0) {
        try out.appendSlice(alloc, "\n// ── Type definitions ────────────────────────────────\n");
        try out.appendSlice(alloc, type_decls);
    }

    // ── Phase 3: Module-level variables ─────────────────────────
    try emitModuleVars(alloc, lex, source, &out);

    // ── Phase 4: Functions ──────────────────────────────────────
    collectEnumNames(lex, source);
    try emitFunctions(alloc, lex, source, &out);

    return try alloc.dupe(u8, out.items);
}

// ── Import handling ─────────────────────────────────────────────────
// import { EventHandler } from './events';
// → const events = @import("events.zig");
//   const EventHandler = events.EventHandler;

fn emitImports(
    alloc: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
    out: *std.ArrayListUnmanaged(u8),
) !void {
    var pos: u32 = 0;
    var has_imports = false;

    while (pos < lex.count) {
        const tok = lex.get(pos);

        // Skip 'declare' statements entirely — they contain 'import' tokens
        // that would confuse the import parser below.
        // Must respect brace depth since @cImport({ @cInclude("..."); }) has
        // semicolons inside braces.
        if (tok.kind == .identifier and std.mem.eql(u8, tok.text(source), "declare")) {
            var brace_depth: u32 = 0;
            while (pos < lex.count) {
                const k = lex.get(pos).kind;
                if (k == .lbrace) brace_depth += 1;
                if (k == .rbrace) { if (brace_depth > 0) brace_depth -= 1; }
                if (k == .semicolon and brace_depth == 0) { pos += 1; break; }
                pos += 1;
            }
            continue;
        }

        if (tok.kind != .identifier or !std.mem.eql(u8, tok.text(source), "import")) {
            pos += 1;
            continue;
        }

        if (!has_imports) {
            try out.appendSlice(alloc, "\n// ── Imports ────────────────────────────────────────\n");
            has_imports = true;
        }

        pos += 1; // skip 'import'

        // ── Default import: import wgpu from 'wgpu' ────────────
        if (pos < lex.count and lex.get(pos).kind == .identifier and
            !std.mem.eql(u8, lex.get(pos).text(source), "type"))
        {
            const next_tok = if (pos + 1 < lex.count) lex.get(pos + 1) else lex.get(pos);
            if (next_tok.kind == .identifier and std.mem.eql(u8, next_tok.text(source), "from")) {
                // Default import: import <name> from '<path>'
                const import_name = lex.get(pos).text(source);
                pos += 1; // skip name
                pos += 1; // skip 'from'
                if (pos >= lex.count or lex.get(pos).kind != .string) {
                    while (pos < lex.count and lex.get(pos).kind != .semicolon) pos += 1;
                    if (pos < lex.count) pos += 1;
                    continue;
                }
                const raw_path = lex.get(pos).text(source);
                pos += 1;
                if (pos < lex.count and lex.get(pos).kind == .semicolon) pos += 1;

                if (raw_path.len < 3) continue;
                const path = raw_path[1 .. raw_path.len - 1];
                // Non-relative path → @import without .zig (package import)
                const is_relative = std.mem.startsWith(u8, path, "./") or std.mem.startsWith(u8, path, "../");
                if (is_relative) {
                    var mod_name: []const u8 = path;
                    if (std.mem.startsWith(u8, mod_name, "./")) mod_name = mod_name[2..];
                    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                        "const {s} = @import(\"{s}.zig\");\n", .{ import_name, mod_name }));
                } else {
                    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                        "const {s} = @import(\"{s}\");\n", .{ import_name, path }));
                }
                continue;
            }
        }

        // ── Named import: import { X, Y as Z } from './mod' ───
        if (pos >= lex.count or lex.get(pos).kind != .lbrace) {
            while (pos < lex.count and lex.get(pos).kind != .semicolon) pos += 1;
            if (pos < lex.count) pos += 1;
            continue;
        }
        pos += 1; // skip {

        // Collect imported names with optional aliases
        const ImportEntry = struct { original: []const u8, alias: []const u8 };
        var entries: [32]ImportEntry = undefined;
        var entry_count: u32 = 0;
        while (pos < lex.count and lex.get(pos).kind != .rbrace) {
            if (lex.get(pos).kind == .comma) { pos += 1; continue; }
            if (lex.get(pos).kind != .identifier) { pos += 1; continue; }
            const original = lex.get(pos).text(source);
            pos += 1;
            // Check for 'as' alias
            if (pos < lex.count and lex.get(pos).kind == .identifier and
                std.mem.eql(u8, lex.get(pos).text(source), "as"))
            {
                pos += 1; // skip 'as'
                if (pos < lex.count and lex.get(pos).kind == .identifier) {
                    const alias = lex.get(pos).text(source);
                    pos += 1;
                    if (entry_count < 32) {
                        entries[entry_count] = .{ .original = original, .alias = alias };
                        entry_count += 1;
                    }
                }
            } else {
                if (entry_count < 32) {
                    entries[entry_count] = .{ .original = original, .alias = original };
                    entry_count += 1;
                }
            }
        }
        if (pos < lex.count and lex.get(pos).kind == .rbrace) pos += 1; // skip }

        // Expect 'from'
        if (pos >= lex.count or !std.mem.eql(u8, lex.get(pos).text(source), "from")) {
            while (pos < lex.count and lex.get(pos).kind != .semicolon) pos += 1;
            if (pos < lex.count) pos += 1;
            continue;
        }
        pos += 1; // skip 'from'

        // Get module path string
        if (pos >= lex.count or lex.get(pos).kind != .string) {
            while (pos < lex.count and lex.get(pos).kind != .semicolon) pos += 1;
            if (pos < lex.count) pos += 1;
            continue;
        }
        const raw_path = lex.get(pos).text(source);
        pos += 1; // skip string
        if (pos < lex.count and lex.get(pos).kind == .semicolon) pos += 1; // skip ;

        // Strip quotes: './events' → events
        if (raw_path.len < 3) continue;
        const path = raw_path[1 .. raw_path.len - 1]; // strip quotes
        var mod_name: []const u8 = path;
        if (std.mem.startsWith(u8, mod_name, "./")) mod_name = mod_name[2..];
        if (std.mem.startsWith(u8, mod_name, "../")) mod_name = std.fs.path.basename(mod_name);

        // Check if any alias conflicts with the module name
        const is_relative = std.mem.startsWith(u8, path, "./") or std.mem.startsWith(u8, path, "../");
        const import_path = if (is_relative)
            try std.fmt.allocPrint(alloc, "\"{s}.zig\"", .{mod_name})
        else
            try std.fmt.allocPrint(alloc, "\"{s}\"", .{mod_name});

        var has_conflict = false;
        for (0..entry_count) |i| {
            if (std.mem.eql(u8, entries[i].alias, mod_name)) { has_conflict = true; break; }
        }

        // If conflict, emit each import directly without module line
        if (has_conflict) {
            for (0..entry_count) |i| {
                try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                    "const {s} = @import({s}).{s};\n", .{ entries[i].alias, import_path, entries[i].original }));
            }
        } else {
            // Emit module import + named imports
            try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                "const {s} = @import({s});\n", .{ mod_name, import_path }));
            for (0..entry_count) |i| {
                try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                    "const {s} = {s}.{s};\n", .{ entries[i].alias, mod_name, entries[i].original }));
            }
        }
    }
}

// ── Module-level variable emission ──────────────────────────────────
// let measureFn: MeasureTextFn | null = null;  → var measure_fn: ?MeasureTextFn = null;
// const LAYOUT_BUDGET = 100000;                 → const LAYOUT_BUDGET: usize = 100000;
// let layoutCount = 0;                          → var layout_count: usize = 0;

fn emitModuleVars(
    alloc: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
    out: *std.ArrayListUnmanaged(u8),
) !void {
    var pos: u32 = 0;
    var has_vars = false;
    var brace_depth: u32 = 0;

    while (pos < lex.count) {
        const tok = lex.get(pos);

        // Track brace depth to only catch top-level declarations
        if (tok.kind == .lbrace) {
            brace_depth += 1;
            pos += 1;
            continue;
        }
        if (tok.kind == .rbrace) {
            if (brace_depth > 0) brace_depth -= 1;
            pos += 1;
            continue;
        }

        // Only process top-level tokens
        if (brace_depth > 0) {
            pos += 1;
            continue;
        }

        // Skip 'declare' statements (FFI declarations handled elsewhere)
        if (tok.kind == .identifier and std.mem.eql(u8, tok.text(source), "declare")) {
            var d_depth: u32 = 0;
            while (pos < lex.count) {
                const k = lex.get(pos).kind;
                if (k == .lbrace) d_depth += 1;
                if (k == .rbrace) { if (d_depth > 0) d_depth -= 1; }
                if (k == .semicolon and d_depth == 0) { pos += 1; break; }
                pos += 1;
            }
            continue;
        }

        // Top-level builtin call as statement: @memcpy(dst, src);
        if (tok.kind == .builtin) {
            if (!has_vars) {
                try out.appendSlice(alloc, "\n// ── Module state ───────────────────────────────────\n");
                has_vars = true;
            }
            const stmt = try collectExprUntil(alloc, lex, source, &pos, .semicolon);
            if (pos < lex.count and lex.get(pos).kind == .semicolon) pos += 1;
            try out.appendSlice(alloc, try std.fmt.allocPrint(alloc, "{s};\n", .{stmt}));
            continue;
        }

        if (tok.kind != .identifier) {
            pos += 1;
            continue;
        }

        const text = tok.text(source);

        // Skip type declarations handled by other phases (skip name + body)
        if (std.mem.eql(u8, text, "enum") or
            std.mem.eql(u8, text, "interface") or
            std.mem.eql(u8, text, "union"))
        {
            pos += 1; // skip keyword
            if (pos < lex.count and lex.get(pos).kind == .identifier) pos += 1; // skip name
            // Skip body braces
            if (pos < lex.count and lex.get(pos).kind == .lbrace) {
                var depth: u32 = 1;
                pos += 1;
                while (pos < lex.count and depth > 0) {
                    if (lex.get(pos).kind == .lbrace) depth += 1;
                    if (lex.get(pos).kind == .rbrace) depth -= 1;
                    pos += 1;
                }
            }
            continue;
        }
        if (std.mem.eql(u8, text, "type") or
            std.mem.eql(u8, text, "import"))
        {
            // Skip the entire declaration through the semicolon
            pos += 1;
            while (pos < lex.count and lex.get(pos).kind != .semicolon) {
                pos += 1;
            }
            if (pos < lex.count and lex.get(pos).kind == .semicolon) pos += 1;
            continue;
        }
        // Skip function declarations (name + params + body)
        if (std.mem.eql(u8, text, "function")) {
            pos += 1; // skip 'function'
            if (pos < lex.count and lex.get(pos).kind == .identifier) pos += 1; // skip name
            // Skip params (...)
            if (pos < lex.count and lex.get(pos).kind == .lparen) {
                var pdepth: u32 = 1;
                pos += 1;
                while (pos < lex.count and pdepth > 0) {
                    if (lex.get(pos).kind == .lparen) pdepth += 1;
                    if (lex.get(pos).kind == .rparen) pdepth -= 1;
                    pos += 1;
                }
            }
            // Skip return type + body
            if (pos < lex.count and lex.get(pos).kind == .colon) {
                pos += 1;
                // Skip type tokens until {
                while (pos < lex.count and lex.get(pos).kind != .lbrace) pos += 1;
            }
            // Skip body braces
            if (pos < lex.count and lex.get(pos).kind == .lbrace) {
                var depth: u32 = 1;
                pos += 1;
                while (pos < lex.count and depth > 0) {
                    if (lex.get(pos).kind == .lbrace) depth += 1;
                    if (lex.get(pos).kind == .rbrace) depth -= 1;
                    pos += 1;
                }
            }
            continue;
        }

        // Track export prefix for pub visibility
        if (std.mem.eql(u8, text, "export")) {
            pos += 1; // skip export, next token should be let/const
            continue;
        }

        if (!std.mem.eql(u8, text, "let") and !std.mem.eql(u8, text, "const")) {
            pos += 1;
            continue;
        }

        // Check if previous token was "export" → make pub
        const is_pub = pos > 0 and lex.get(pos - 1).kind == .identifier and
            std.mem.eql(u8, lex.get(pos - 1).text(source), "export");

        // Found a top-level let/const
        const is_const = std.mem.eql(u8, text, "const");
        pos += 1; // skip let/const

        if (pos >= lex.count or lex.get(pos).kind != .identifier) continue;
        const name = lex.get(pos).text(source);
        pos += 1; // skip name

        if (!has_vars) {
            try out.appendSlice(alloc, "\n// ── Module state ───────────────────────────────────\n");
            has_vars = true;
        }

        const zig_keyword = if (is_pub and is_const) "pub const" else if (is_pub) "pub var" else if (is_const) "const" else "var";
        // Keep variable names in original casing to match references in function bodies
        const snake_name = name;

        // Parse optional type annotation: `: Type` or `: Type | null`
        var type_str: ?[]const u8 = null;
        var is_nullable = false;

        if (pos < lex.count and lex.get(pos).kind == .colon) {
            pos += 1; // skip :
            type_str = try parseTypeAtPos(alloc, lex, source, &pos);

            // Check for | null
            if (pos < lex.count and lex.get(pos).kind == .pipe) {
                pos += 1; // skip |
                if (pos < lex.count and lex.get(pos).kind == .identifier and
                    std.mem.eql(u8, lex.get(pos).text(source), "null"))
                {
                    is_nullable = true;
                    pos += 1; // skip null
                }
            }
        }

        // Parse initializer: = expr
        var init_val: ?[]const u8 = null;
        if (pos < lex.count and lex.get(pos).kind == .equals) {
            pos += 1; // skip =
            init_val = try collectExprUntil(alloc, lex, source, &pos, .semicolon);
        }

        // Skip ;
        if (pos < lex.count and lex.get(pos).kind == .semicolon) pos += 1;

        // Emit the variable
        if (type_str) |ts| {
            const zig_type = try typegen.mapType(alloc, ts);
            if (is_nullable) {
                try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                    "{s} {s}: ?{s}", .{ zig_keyword, snake_name, zig_type }));
            } else {
                try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                    "{s} {s}: {s}", .{ zig_keyword, snake_name, zig_type }));
            }
        } else {
            // Infer type from initializer
            if (init_val) |iv| {
                if (isNumericLiteral(iv) and isAllCaps(name)) {
                    // ALL_CAPS constants are typically counts/limits → usize
                    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                        "{s} {s}: usize", .{ zig_keyword, snake_name }));
                } else if (isNumericLiteral(iv)) {
                    // TS number semantics → f32 (not usize)
                    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                        "{s} {s}: f32", .{ zig_keyword, snake_name }));
                } else {
                    try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                        "{s} {s}", .{ zig_keyword, snake_name }));
                }
            } else {
                try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                    "{s} {s}", .{ zig_keyword, snake_name }));
            }
        }

        if (init_val) |iv| {
            try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                " = {s};\n", .{translateInitValue(alloc, iv)}));
        } else {
            try out.appendSlice(alloc, " = undefined;\n");
        }
    }

    if (has_vars) try out.appendSlice(alloc, "\n");
}

// ── Function emission ───────────────────────────────────────────────
// function padLeft(s: Style): number { ... }
// → fn padLeft(s: *const Style) f32 { ... }
//
// export function layout(root: Node, ...): void { ... }
// → pub fn layout(root: *Node, ...) void { ... }

/// Pre-scan all function signatures to register pointer param types.
/// This allows forward references: a function can call another function
/// defined later and still get correct & insertion.
fn prescanFunctionSigs(
    _: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
) !void {
    var pos: u32 = 0;
    var brace_depth: u32 = 0;

    while (pos < lex.count) {
        const tok = lex.get(pos);
        if (tok.kind == .lbrace) { brace_depth += 1; pos += 1; continue; }
        if (tok.kind == .rbrace) { if (brace_depth > 0) brace_depth -= 1; pos += 1; continue; }
        if (brace_depth > 0 or tok.kind != .identifier) { pos += 1; continue; }

        const text = tok.text(source);
        if (std.mem.eql(u8, text, "export")) { pos += 1; continue; }

        if (std.mem.eql(u8, text, "function") or
            (pos > 0 and lex.get(pos).kind == .identifier and std.mem.eql(u8, text, "function")))
        {
            pos += 1; // skip "function"
            if (pos >= lex.count or lex.get(pos).kind != .identifier) continue;
            const fn_name = lex.get(pos).text(source);
            pos += 1; // skip name

            // Parse params to register pointer types
            if (pos < lex.count and lex.get(pos).kind == .lparen) {
                pos += 1; // skip (
                var param_idx: u32 = 0;
                while (pos < lex.count and lex.get(pos).kind != .rparen) {
                    if (lex.get(pos).kind != .identifier) { pos += 1; continue; }
                    pos += 1; // skip param name
                    if (pos < lex.count and lex.get(pos).kind == .colon) pos += 1; // skip :
                    // Read type name
                    const param_type = if (pos < lex.count and lex.get(pos).kind == .identifier)
                        lex.get(pos).text(source)
                    else
                        "";
                    if (param_type.len > 0) pos += 1; // skip type
                    // Check for | null
                    if (pos < lex.count and lex.get(pos).kind == .pipe) {
                        pos += 1;
                        if (pos < lex.count and lex.get(pos).kind == .identifier) pos += 1;
                    }
                    // Register if struct type (Node or PascalCase)
                    if (param_type.len > 0 and isStructTypeName(param_type)) {
                        stmtgen.registerFnPtrParam(fn_name, param_idx);
                    }
                    param_idx += 1;
                    if (pos < lex.count and lex.get(pos).kind == .comma) pos += 1;
                }
                if (pos < lex.count and lex.get(pos).kind == .rparen) pos += 1;
            }
            // Skip rest of function (return type + body)
            while (pos < lex.count and lex.get(pos).kind != .lbrace) pos += 1;
            if (pos < lex.count and lex.get(pos).kind == .lbrace) {
                var depth: u32 = 1;
                pos += 1;
                while (pos < lex.count and depth > 0) {
                    if (lex.get(pos).kind == .lbrace) depth += 1;
                    if (lex.get(pos).kind == .rbrace) depth -= 1;
                    pos += 1;
                }
            }
            continue;
        }
        pos += 1;
    }
}

fn emitFunctions(
    alloc: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
    out: *std.ArrayListUnmanaged(u8),
) !void {
    // Pre-scan to register all function param types for forward references
    try prescanFunctionSigs(alloc, lex, source);

    var pos: u32 = 0;
    var brace_depth: u32 = 0;
    var has_functions = false;

    while (pos < lex.count) {
        const tok = lex.get(pos);

        // Track brace depth for top-level detection
        if (tok.kind == .lbrace) {
            brace_depth += 1;
            pos += 1;
            continue;
        }
        if (tok.kind == .rbrace) {
            if (brace_depth > 0) brace_depth -= 1;
            pos += 1;
            continue;
        }

        // Only process top-level tokens
        if (brace_depth > 0 or tok.kind != .identifier) {
            pos += 1;
            continue;
        }

        const text = tok.text(source);

        var is_pub = false;
        if (std.mem.eql(u8, text, "export")) {
            is_pub = true;
            pos += 1;
            if (pos >= lex.count) break;
        }

        if (pos < lex.count and lex.get(pos).kind == .identifier and
            std.mem.eql(u8, lex.get(pos).text(source), "function"))
        {
            pos += 1; // skip "function"
            if (pos >= lex.count) break;

            // Function name
            if (lex.get(pos).kind != .identifier) continue;
            const name = lex.get(pos).text(source);
            pos += 1;

            if (!has_functions) {
                try out.appendSlice(alloc, "// ── Functions ──────────────────────────────────────\n");
                has_functions = true;
            }

            const pub_prefix = if (is_pub) "pub " else "";

            // Parse parameters: (name: Type, name: Type | null, ...)
            // Keep function names in original camelCase — matches Zig convention
            // in the hand-written runtime and avoids caller/callee mismatches
            try out.appendSlice(alloc, try std.fmt.allocPrint(alloc,
                "\n{s}fn {s}(", .{ pub_prefix, name }));

            // Reset var types BEFORE registering params
            stmtgen.resetVarTypes();

            if (pos < lex.count and lex.get(pos).kind == .lparen) {
                pos += 1; // skip (
                try emitParams(alloc, lex, source, &pos, out, name);
                // pos is now past the )
            }

            try out.appendSlice(alloc, ") ");

            // Parse return type and optional callconv
            var ret_str: []const u8 = "void";
            var callconv_str: ?[]const u8 = null;

            if (pos < lex.count and lex.get(pos).kind == .colon) {
                pos += 1; // skip :

                // Check for ! prefix (error union return type)
                var error_union = false;
                if (pos < lex.count and lex.get(pos).kind == .bang) {
                    error_union = true;
                    pos += 1; // skip !
                }

                // Parse return type (may include | null)
                const ret_type = try parseTypeAtPos(alloc, lex, source, &pos);
                var ret_nullable = false;
                if (pos < lex.count and lex.get(pos).kind == .pipe) {
                    pos += 1;
                    if (pos < lex.count and lex.get(pos).kind == .identifier and
                        std.mem.eql(u8, lex.get(pos).text(source), "null"))
                    {
                        ret_nullable = true;
                        pos += 1;
                    }
                }

                const zig_ret = try typegen.mapType(alloc, ret_type);
                const base_ret = if (ret_nullable and isStructTypeName(ret_type))
                    try std.fmt.allocPrint(alloc, "?*{s}", .{zig_ret})
                else if (ret_nullable)
                    try std.fmt.allocPrint(alloc, "?{s}", .{zig_ret})
                else
                    zig_ret;
                ret_str = if (error_union)
                    try std.fmt.allocPrint(alloc, "!{s}", .{base_ret})
                else
                    base_ret;
            }

            // Register function return type for call-site type resolution
            stmtgen.registerFnReturnType(name, stmtgen.typeStrToExprType(ret_str));

            // Check for callconv(...) — emitted before return type in Zig
            if (pos < lex.count and lex.get(pos).kind == .identifier and
                std.mem.eql(u8, lex.get(pos).text(source), "callconv"))
            {
                pos += 1; // skip callconv
                var cc_buf: std.ArrayListUnmanaged(u8) = .{};
                try cc_buf.appendSlice(alloc, "callconv(");
                if (pos < lex.count and lex.get(pos).kind == .lparen) pos += 1;
                while (pos < lex.count and lex.get(pos).kind != .rparen) {
                    try cc_buf.appendSlice(alloc, lex.get(pos).text(source));
                    pos += 1;
                }
                if (pos < lex.count and lex.get(pos).kind == .rparen) pos += 1;
                try cc_buf.appendSlice(alloc, ")");
                callconv_str = try alloc.dupe(u8, cc_buf.items);
            }

            // Emit: callconv(.c) RetType (callconv before return type in Zig)
            if (callconv_str) |cc| {
                try out.appendSlice(alloc, cc);
                try out.append(alloc, ' ');
            }
            try out.appendSlice(alloc, ret_str);

            try out.appendSlice(alloc, " {\n");

            // Parse body with stmtgen
            if (pos < lex.count and lex.get(pos).kind == .lbrace) {
                const body = try stmtgen.emitBlock(alloc, lex, source, &pos, 1);
                try out.appendSlice(alloc, body);
            }

            try out.appendSlice(alloc, "}\n");
            continue;
        }

        if (!is_pub) pos += 1;
    }
}

// ── Parameter emission ──────────────────────────────────────────────
// Parses: name: Type, name: Type | null, ...
// Emits:  name: *Type, name: ?f32, ...
// pos should be past the opening (. Advances past the closing ).

fn emitParams(
    alloc: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
    pos: *u32,
    out: *std.ArrayListUnmanaged(u8),
    fn_name: []const u8,
) !void {
    var first = true;
    var param_idx: u32 = 0;

    while (pos.* < lex.count and lex.get(pos.*).kind != .rparen) {
        if (lex.get(pos.*).kind != .identifier) {
            pos.* += 1;
            continue;
        }

        if (!first) try out.appendSlice(alloc, ", ");
        first = false;

        // Parameter name
        const param_name = lex.get(pos.*).text(source);
        pos.* += 1;

        // Expect :
        if (pos.* < lex.count and lex.get(pos.*).kind == .colon) {
            pos.* += 1; // skip :
        }

        // Parse type
        const param_type = try parseTypeAtPos(alloc, lex, source, pos);

        // Check for | null
        var param_nullable = false;
        if (pos.* < lex.count and lex.get(pos.*).kind == .pipe) {
            pos.* += 1;
            if (pos.* < lex.count and lex.get(pos.*).kind == .identifier and
                std.mem.eql(u8, lex.get(pos.*).text(source), "null"))
            {
                param_nullable = true;
                pos.* += 1;
            }
        }

        const zig_type = try typegen.mapType(alloc, param_type);
        // Keep param names in original casing to match body references
        const escaped_param = if (typegen.isZigKeyword(param_name))
            try std.fmt.allocPrint(alloc, "@\"{s}\"", .{param_name})
        else
            param_name;

        try out.appendSlice(alloc, escaped_param);
        try out.appendSlice(alloc, ": ");

        if (param_nullable) {
            try out.appendSlice(alloc, try std.fmt.allocPrint(alloc, "?{s}", .{zig_type}));
            stmtgen.registerVar(param_name, .opt_f32_t);
        } else if (std.mem.eql(u8, param_type, "Node")) {
            // Node params → mutable pointer (layout engine mutates nodes)
            try out.appendSlice(alloc, try std.fmt.allocPrint(alloc, "*{s}", .{zig_type}));
            stmtgen.registerVar(param_name, .ptr_t);
            stmtgen.registerFnPtrParam(fn_name, param_idx);
        } else {
            // All other types (including structs) → pass by value.
            // If the .tsz author wants pointer semantics, they write *const Type explicitly.
            try out.appendSlice(alloc, zig_type);
            // Register param type for expression type inference
            const expr_ty: exprgen.ExprType = if (std.mem.eql(u8, zig_type, "f32"))
                .f32_t
            else if (std.mem.eql(u8, zig_type, "usize"))
                .usize_t
            else if (std.mem.eql(u8, zig_type, "u16"))
                .u16_t
            else if (std.mem.eql(u8, zig_type, "bool"))
                .bool_t
            else
                .unknown;
            stmtgen.registerVar(param_name, expr_ty);
        }

        param_idx += 1;

        // Skip comma
        if (pos.* < lex.count and lex.get(pos.*).kind == .comma) pos.* += 1;
    }

    // Skip closing )
    if (pos.* < lex.count and lex.get(pos.*).kind == .rparen) pos.* += 1;
}

// ── Type parsing helpers ────────────────────────────────────────────

/// Parse a type annotation at current position (e.g., "number", "Node[]", "MeasureTextFn")
fn parseTypeAtPos(
    alloc: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
    pos: *u32,
) ![]const u8 {
    // [N]T — fixed-size array type
    if (pos.* < lex.count and lex.get(pos.*).kind == .lbracket) {
        pos.* += 1; // skip [
        var size_buf: std.ArrayListUnmanaged(u8) = .{};
        while (pos.* < lex.count and lex.get(pos.*).kind != .rbracket) {
            try size_buf.appendSlice(alloc, lex.get(pos.*).text(source));
            pos.* += 1;
        }
        if (pos.* < lex.count and lex.get(pos.*).kind == .rbracket) pos.* += 1;
        // Parse the element type
        const elem = try parseTypeAtPos(alloc, lex, source, pos);
        const mapped = try typegen.mapType(alloc, elem);
        return try std.fmt.allocPrint(alloc, "[{s}]{s}", .{ size_buf.items, mapped });
    }

    // ?T — optional type prefix
    if (pos.* < lex.count and lex.get(pos.*).kind == .question) {
        pos.* += 1;
        const inner = try parseTypeAtPos(alloc, lex, source, pos);
        const mapped = try typegen.mapType(alloc, inner);
        return try std.fmt.allocPrint(alloc, "?{s}", .{mapped});
    }

    // *T or *const T — pointer type prefix
    if (pos.* < lex.count and lex.get(pos.*).kind == .star) {
        pos.* += 1;
        if (pos.* < lex.count and lex.get(pos.*).kind == .identifier and
            std.mem.eql(u8, lex.get(pos.*).text(source), "const"))
        {
            pos.* += 1;
            const inner = try parseTypeAtPos(alloc, lex, source, pos);
            return try std.fmt.allocPrint(alloc, "*const {s}", .{inner});
        }
        const inner = try parseTypeAtPos(alloc, lex, source, pos);
        return try std.fmt.allocPrint(alloc, "*{s}", .{inner});
    }

    if (pos.* >= lex.count or lex.get(pos.*).kind != .identifier) return "unknown";

    // Read identifier, then consume dotted chain (e.g., c.SDL_Renderer, layout.Node)
    var type_buf: std.ArrayListUnmanaged(u8) = .{};
    try type_buf.appendSlice(alloc, lex.get(pos.*).text(source));
    pos.* += 1;
    while (pos.* + 1 < lex.count and lex.get(pos.*).kind == .dot and
        lex.get(pos.* + 1).kind == .identifier)
    {
        try type_buf.append(alloc, '.');
        pos.* += 1; // skip .
        try type_buf.appendSlice(alloc, lex.get(pos.*).text(source));
        pos.* += 1; // skip identifier
    }
    var t = try alloc.dupe(u8, type_buf.items);

    // const T — type qualifier (e.g., const u8, const Node)
    if (std.mem.eql(u8, t, "const") and pos.* < lex.count and lex.get(pos.*).kind == .identifier) {
        const inner = try parseTypeAtPos(alloc, lex, source, pos);
        t = try std.fmt.allocPrint(alloc, "const {s}", .{inner});
    }

    // fn (params) RetType — function type
    if (std.mem.eql(u8, t, "fn") and pos.* < lex.count and lex.get(pos.*).kind == .lparen) {
        var fn_buf: std.ArrayListUnmanaged(u8) = .{};
        try fn_buf.appendSlice(alloc, "fn ");
        // Collect ( ... ) with proper spacing
        var pdepth: u32 = 0;
        while (pos.* < lex.count) {
            const k = lex.get(pos.*).kind;
            if (k == .lparen) pdepth += 1;
            if (k == .rparen) pdepth -= 1;
            // Add space between identifier-like tokens (not after ( or before ) or after *)
            if (fn_buf.items.len > 0) {
                const prev = fn_buf.items[fn_buf.items.len - 1];
                const needs_space = (k == .identifier or k == .star or k == .question) and
                    prev != '(' and prev != '*' and prev != '?';
                if (needs_space) try fn_buf.append(alloc, ' ');
            }
            try fn_buf.appendSlice(alloc, lex.get(pos.*).text(source));
            pos.* += 1;
            if (pdepth == 0) break;
        }
        // Skip whitespace and collect return type token
        if (pos.* < lex.count and lex.get(pos.*).kind == .identifier) {
            try fn_buf.append(alloc, ' ');
            try fn_buf.appendSlice(alloc, lex.get(pos.*).text(source));
            pos.* += 1;
        }
        return try alloc.dupe(u8, fn_buf.items);
    }

    // Check for T[] (array/slice type)
    if (pos.* + 1 < lex.count and
        lex.get(pos.*).kind == .lbracket and
        lex.get(pos.* + 1).kind == .rbracket)
    {
        pos.* += 2; // skip []
        // Return with [] suffix so mapType can handle it
        return std.fmt.allocPrint(alloc, "{s}[]", .{t}) catch t;
    }

    return t;
}

/// Collect expression tokens until a specific terminator.
fn collectExprUntil(
    alloc: std.mem.Allocator,
    lex: *const Lexer,
    source: []const u8,
    pos: *u32,
    terminator: TokenKind,
) ![]const u8 {
    var result: std.ArrayListUnmanaged(u8) = .{};
    var depth: u32 = 0;

    while (pos.* < lex.count) {
        const kind = lex.get(pos.*).kind;
        if (depth == 0 and kind == terminator) break;
        if (kind == .lparen or kind == .lbrace or kind == .lbracket) depth += 1;
        if (kind == .rparen or kind == .rbrace or kind == .rbracket) {
            if (depth == 0) break;
            depth -= 1;
        }

        if (result.items.len > 0) {
            // Add space between tokens, but not in function call syntax
            const prev_last = result.items[result.items.len - 1];
            const cur_text = lex.get(pos.*).text(source);
            const cur_first = if (cur_text.len > 0) cur_text[0] else @as(u8, 0);
            // Dot suppresses space for member access (a.b), but NOT for
            // enum literals after operators (== .windows)
            const is_member_dot = prev_last == '.' or
                (cur_first == '.' and isIdentChar(prev_last));
            const is_call = cur_first == '(' and (isIdentChar(prev_last) or prev_last == ')');
            const is_close = cur_first == ')' or cur_first == ']';
            const after_open = prev_last == '(' or prev_last == '[';
            const is_comma = cur_first == ',';
            if (!is_member_dot and !is_call and !is_close and !after_open and !is_comma) {
                try result.append(alloc, ' ');
            }
        }
        try result.appendSlice(alloc, lex.get(pos.*).text(source));
        pos.* += 1;
    }

    return try result.toOwnedSlice(alloc);
}

// ── Value translation helpers ───────────────────────────────────────

/// Check if a type name is likely a struct (PascalCase, not a primitive, not an enum).
fn isStructTypeName(name: []const u8) bool {
    if (name.len == 0) return false;
    // Primitives are never structs
    if (std.mem.eql(u8, name, "number") or
        std.mem.eql(u8, name, "string") or
        std.mem.eql(u8, name, "boolean") or
        std.mem.eql(u8, name, "void"))
    {
        return false;
    }
    // Check against known enum names
    for (known_enum_names[0..known_enum_count]) |en| {
        if (std.mem.eql(u8, name, en)) return false;
    }
    // Also exclude function pointer types (end with "Fn")
    if (name.len > 2 and std.mem.endsWith(u8, name, "Fn")) return false;
    // PascalCase (starts with uppercase) → likely a struct
    return name[0] >= 'A' and name[0] <= 'Z';
}

// Module-level enum registry — populated by collectEnumNames before function emission
const MAX_ENUM_NAMES = 64;
var known_enum_names: [MAX_ENUM_NAMES][]const u8 = undefined;
var known_enum_count: u32 = 0;

/// Pre-scan the token stream to collect all enum type names.
fn collectEnumNames(lex: *const Lexer, source: []const u8) void {
    known_enum_count = 0;
    var pos: u32 = 0;
    while (pos < lex.count) {
        const tok = lex.get(pos);
        if (tok.kind == .identifier and std.mem.eql(u8, tok.text(source), "enum")) {
            pos += 1; // skip "enum"
            if (pos < lex.count and lex.get(pos).kind == .identifier) {
                if (known_enum_count < MAX_ENUM_NAMES) {
                    known_enum_names[known_enum_count] = lex.get(pos).text(source);
                    known_enum_count += 1;
                }
            }
        }
        pos += 1;
    }
}

fn isIdentChar(ch: u8) bool {
    return (ch >= 'a' and ch <= 'z') or (ch >= 'A' and ch <= 'Z') or (ch >= '0' and ch <= '9') or ch == '_';
}

fn isNumericLiteral(s: []const u8) bool {
    if (s.len == 0) return false;
    return (s[0] >= '0' and s[0] <= '9') or
        (s[0] == '-' and s.len > 1 and s[1] >= '0' and s[1] <= '9');
}

fn isAllCaps(name: []const u8) bool {
    for (name) |c| {
        if (c >= 'a' and c <= 'z') return false;
    }
    return name.len > 0;
}

fn translateInitValue(alloc: std.mem.Allocator, val: []const u8) []const u8 {
    if (std.mem.eql(u8, val, "null")) return "null";
    if (std.mem.eql(u8, val, "true")) return "true";
    if (std.mem.eql(u8, val, "false")) return "false";
    // Object literal { key: val } → .{ .key = val }
    if (val.len > 0 and val[0] == '{') {
        // Convert TS-style { key : val } to Zig .{ .key = val }
        return std.fmt.allocPrint(alloc, ".{s}", .{val}) catch val;
    }
    return val;
}
