#!/usr/bin/env python3
"""
keygen.py — Generate Ed25519 keypair for CartridgeOS cart signing.

Outputs:
  dev-key.pub     — 32-byte public key (hex + raw)
  dev-key.secret  — 64-byte secret key (hex + raw)
  init_pubkey.h   — C header with the public key array

Usage:
  python3 keygen.py [--prefix <name>]
"""

import sys
import hashlib

try:
    from nacl.signing import SigningKey
except ImportError:
    print("ERROR: PyNaCl not installed. Run: pip install pynacl", file=sys.stderr)
    sys.exit(1)


def main():
    prefix = "dev-key"
    if "--prefix" in sys.argv:
        idx = sys.argv.index("--prefix")
        prefix = sys.argv[idx + 1]

    # Generate keypair
    sk = SigningKey.generate()
    pk = sk.verify_key

    # Raw bytes
    sk_bytes = bytes(sk) + bytes(pk)  # Ed25519 secret key is seed(32) + pubkey(32) = 64 bytes
    pk_bytes = bytes(pk)

    # Write raw files
    with open(f"{prefix}.pub", "wb") as f:
        f.write(pk_bytes)

    with open(f"{prefix}.secret", "wb") as f:
        f.write(sk_bytes)

    # Compute key_id (first 8 bytes of SHA-512 of pubkey, matching init.c)
    key_id = hashlib.sha512(pk_bytes).digest()[:8]

    # Write C header for embedding in init.c
    hex_arr = ", ".join(f"0x{b:02x}" for b in pk_bytes)
    key_id_hex = key_id.hex()

    with open("init_pubkey.h", "w") as f:
        f.write("/* Auto-generated by keygen.py — CartridgeOS trust root */\n")
        f.write(f"/* key_id: {key_id_hex} */\n")
        f.write(f"static const uint8_t OS_PUBKEY[{len(pk_bytes)}] = {{\n")
        # Format as 8 bytes per line
        for i in range(0, len(pk_bytes), 8):
            chunk = pk_bytes[i:i+8]
            line = ", ".join(f"0x{b:02x}" for b in chunk)
            f.write(f"    {line},\n")
        f.write("};\n")

    print(f"  Generated Ed25519 keypair:")
    print(f"    Public key:  {prefix}.pub  ({len(pk_bytes)} bytes)")
    print(f"    Secret key:  {prefix}.secret  ({len(sk_bytes)} bytes)")
    print(f"    C header:    init_pubkey.h")
    print(f"    Key ID:      {key_id_hex}")
    print(f"    Public key:  {pk_bytes.hex()}")
    print()
    print(f"  To embed in init.c, replace the OS_PUBKEY array with")
    print(f"  the contents of init_pubkey.h")


if __name__ == "__main__":
    main()
