/* ── Helpers ─────────────────────────────────────────────────────────────── */

/* TweetNaCl requires this symbol. PID 1 never generates keys,
 * so this is a fatal stub — if called, something is very wrong. */
void randombytes(unsigned char *buf, unsigned long long len) {
    (void)buf; (void)len;
    fprintf(stderr, "[init] FATAL: randombytes called in PID 1\n");
    _exit(99);
}

static void run_wait(char *const argv[]) {
    pid_t pid = fork();
    if (pid == 0) { execv(argv[0], argv); _exit(1); }
    if (pid > 0)  waitpid(pid, NULL, 0);
}

static void hex_encode(const uint8_t *data, size_t len, char *out) {
    static const char hex[] = "0123456789abcdef";
    for (size_t i = 0; i < len; i++) {
        out[i*2]     = hex[data[i] >> 4];
        out[i*2 + 1] = hex[data[i] & 0x0f];
    }
    out[len*2] = '\0';
}

/* ── Kernel cmdline parsing ──────────────────────────────────────────────── */

static int cmdline_has(const char *key) {
    char buf[4096];
    int fd = open("/proc/cmdline", O_RDONLY);
    if (fd < 0) return 0;
    ssize_t n = read(fd, buf, sizeof(buf) - 1);
    close(fd);
    if (n <= 0) return 0;
    buf[n] = '\0';
    return strstr(buf, key) != NULL;
}

static int cmdline_get(const char *key, char *out, size_t outlen) {
    char buf[4096];
    int fd = open("/proc/cmdline", O_RDONLY);
    if (fd < 0) return 0;
    ssize_t n = read(fd, buf, sizeof(buf) - 1);
    close(fd);
    if (n <= 0) return 0;
    buf[n] = '\0';

    size_t klen = strlen(key);
    char *p = buf;
    while ((p = strstr(p, key)) != NULL) {
        /* Ensure it's a full match (preceded by space or start of string) */
        if (p != buf && *(p-1) != ' ') { p += klen; continue; }
        if (p[klen] != '=') { p += klen; continue; }
        p += klen + 1;
        /* Copy value until space or end */
        size_t i = 0;
        while (*p && *p != ' ' && *p != '\n' && i < outlen - 1)
            out[i++] = *p++;
        out[i] = '\0';
        return 1;
    }
    return 0;
}

/* ── .cart verification ──────────────────────────────────────────────────── */

/* Detached Ed25519 verification using TweetNaCl's signed-message API.
 * Constructs: signed_buf = signature(64) + message(msglen)
 * Then calls crypto_sign_open to verify. */
static int ed25519_verify_detached(
    const uint8_t *sig, const uint8_t *msg, size_t msglen,
    const uint8_t *pk)
{
    /* Stack buffer: sig(64) + msg. For .cart, msg = header = 160 bytes. */
    uint8_t signed_buf[64 + CART_HEADER_SIZE];
    /* crypto_sign_open writes ALL n bytes to m[] before verifying,
     * so the output buffer must be at least as large as the input. */
    uint8_t recovered[64 + CART_HEADER_SIZE];
    unsigned long long recovered_len;

    if (msglen > CART_HEADER_SIZE) return -1;  /* safety */

    memcpy(signed_buf, sig, 64);
    memcpy(signed_buf + 64, msg, msglen);

    if (crypto_sign_open(recovered, &recovered_len,
                         signed_buf, 64 + msglen, pk) != 0)
        return -1;

    return 0;
}

/* Compute key_id: first 8 bytes of SHA-256(pubkey).
 * We use our SHA-512 and truncate (SHA-256 isn't strictly needed;
 * truncated SHA-512 is equally good for an 8-byte identifier). */
static void compute_key_id(const uint8_t *pubkey, uint8_t *key_id) {
    uint8_t hash[SHA512_DIGEST_SIZE];
    sha512(pubkey, CART_PUBKEY_SIZE, hash);
    memcpy(key_id, hash, CART_KEY_ID_SIZE);
}

/* Verify a .cart file. Returns verdict code. */
static int verify_cart(const char *path, struct cart_header *hdr_out) {
    FILE *f = fopen(path, "rb");
    if (!f) {
        fprintf(stderr, "[init] cannot open cart: %s\n", path);
        return CART_VERDICT_NO_CART;
    }

    /* Read header */
    struct cart_header hdr;
    if (fread(&hdr, 1, CART_HEADER_SIZE, f) != CART_HEADER_SIZE) {
        fprintf(stderr, "[init] cart too small for header\n");
        fclose(f);
        return CART_VERDICT_BAD_FORMAT;
    }

    /* Validate magic and version */
    if (memcmp(hdr.magic, CART_MAGIC, CART_MAGIC_LEN) != 0) {
        fprintf(stderr, "[init] bad magic (not a .cart)\n");
        fclose(f);
        return CART_VERDICT_BAD_FORMAT;
    }
    if (hdr.version != CART_VERSION) {
        fprintf(stderr, "[init] unsupported cart version: %d\n", hdr.version);
        fclose(f);
        return CART_VERDICT_BAD_FORMAT;
    }

    /* Check key_id matches our pubkey */
    uint8_t expected_key_id[CART_KEY_ID_SIZE];
    compute_key_id(OS_PUBKEY, expected_key_id);
    if (memcmp(hdr.key_id, expected_key_id, CART_KEY_ID_SIZE) != 0) {
        char got[17], want[17];
        hex_encode(hdr.key_id, CART_KEY_ID_SIZE, got);
        hex_encode(expected_key_id, CART_KEY_ID_SIZE, want);
        fprintf(stderr, "[init] key_id mismatch: cart=%s os=%s\n", got, want);
        fclose(f);
        return CART_VERDICT_BAD_SIG;
    }

    /* Seek to signature (after header + manifest + payload) */
    uint64_t sig_offset = CART_HEADER_SIZE + hdr.manifest_len + hdr.payload_len;
    if (fseek(f, (long)sig_offset, SEEK_SET) != 0) {
        fprintf(stderr, "[init] cannot seek to signature\n");
        fclose(f);
        return CART_VERDICT_BAD_FORMAT;
    }

    uint8_t sig[CART_SIG_SIZE];
    if (fread(sig, 1, CART_SIG_SIZE, f) != CART_SIG_SIZE) {
        fprintf(stderr, "[init] cannot read signature\n");
        fclose(f);
        return CART_VERDICT_BAD_FORMAT;
    }

    /* ── Step 1: Verify Ed25519 signature over the 160 raw header bytes ──── */
    printf("  Verifying Ed25519 signature...\n");
    if (ed25519_verify_detached(sig, (const uint8_t *)&hdr,
                                 CART_HEADER_SIZE, OS_PUBKEY) != 0) {
        fprintf(stderr, "[init] SIGNATURE VERIFICATION FAILED\n");
        fclose(f);
        return CART_VERDICT_BAD_SIG;
    }
    puts("  Signature: VALID");

    /* ── Step 2: Verify manifest hash ──────────────────────────────────── */
    printf("  Verifying manifest hash (%u bytes)...\n", hdr.manifest_len);
    fseek(f, CART_HEADER_SIZE, SEEK_SET);

    struct sha512_ctx hash_ctx;
    uint8_t chunk[4096];
    uint8_t computed_hash[SHA512_DIGEST_SIZE];

    sha512_init(&hash_ctx);
    uint32_t remaining = hdr.manifest_len;
    while (remaining > 0) {
        size_t to_read = remaining < sizeof(chunk) ? remaining : sizeof(chunk);
        size_t got = fread(chunk, 1, to_read, f);
        if (got == 0) {
            fprintf(stderr, "[init] manifest read error\n");
            fclose(f);
            return CART_VERDICT_BAD_HASH;
        }
        sha512_update(&hash_ctx, chunk, got);
        remaining -= (uint32_t)got;
    }
    sha512_final(&hash_ctx, computed_hash);

    if (memcmp(computed_hash, hdr.manifest_hash, SHA512_DIGEST_SIZE) != 0) {
        fprintf(stderr, "[init] MANIFEST HASH MISMATCH\n");
        fclose(f);
        return CART_VERDICT_BAD_HASH;
    }
    puts("  Manifest hash: VALID");

    /* ── Step 3: Verify payload hash ────────────────────────────────── */
    printf("  Verifying payload hash (%llu bytes)...\n",
           (unsigned long long)hdr.payload_len);

    sha512_init(&hash_ctx);
    uint64_t payload_remaining = hdr.payload_len;
    while (payload_remaining > 0) {
        size_t to_read = payload_remaining < sizeof(chunk)
                       ? (size_t)payload_remaining : sizeof(chunk);
        size_t got = fread(chunk, 1, to_read, f);
        if (got == 0) {
            fprintf(stderr, "[init] payload read error\n");
            fclose(f);
            return CART_VERDICT_BAD_HASH;
        }
        sha512_update(&hash_ctx, chunk, got);
        payload_remaining -= got;
    }
    sha512_final(&hash_ctx, computed_hash);

    if (memcmp(computed_hash, hdr.payload_hash, SHA512_DIGEST_SIZE) != 0) {
        fprintf(stderr, "[init] PAYLOAD HASH MISMATCH\n");
        fclose(f);
        return CART_VERDICT_BAD_HASH;
    }
    puts("  Payload hash: VALID");

    fclose(f);
    memcpy(hdr_out, &hdr, sizeof(hdr));
    return CART_VERDICT_VERIFIED;
}

/* ── Secure cpio extraction ──────────────────────────────────────────────── */

/* Extract payload cpio from .cart to dest_dir.
 * Uses busybox cpio in a safe way:
 *   - Extracts to a fresh tmpfs (caller mounts it)
 *   - Post-extraction: scan for path traversal, symlink escapes
 */
static int extract_payload(const char *cart_path, uint64_t payload_offset,
                           uint64_t payload_len, const char *dest_dir)
{
    printf("  Extracting payload to %s...\n", dest_dir);

    /* Open the .cart file and seek past header + manifest */
    FILE *cart = fopen(cart_path, "rb");
    if (!cart) {
        fprintf(stderr, "[init] cannot open cart: %s\n", cart_path);
        return -1;
    }
    fseek(cart, CART_HEADER_SIZE + payload_offset, SEEK_SET);

    /* Pipe payload bytes directly into busybox cpio */
    char cpio_cmd[512];
    snprintf(cpio_cmd, sizeof(cpio_cmd),
        "cd '%s' && /bin/busybox cpio -i -d -u", dest_dir);

    FILE *pipe = popen(cpio_cmd, "w");
    if (!pipe) {
        fprintf(stderr, "[init] cannot start cpio\n");
        fclose(cart);
        return -1;
    }

    uint8_t buf[65536];
    uint64_t remaining = payload_len;
    while (remaining > 0) {
        size_t chunk = remaining < sizeof(buf) ? (size_t)remaining : sizeof(buf);
        size_t got = fread(buf, 1, chunk, cart);
        if (got == 0) break;
        fwrite(buf, 1, got, pipe);
        remaining -= got;
    }
    fclose(cart);

    int rc = pclose(pipe);
    if (rc != 0) {
        fprintf(stderr, "[init] cpio extraction failed (rc=%d)\n", rc);
        return -1;
    }

    /* ── Security scan: reject path traversal and symlink escapes ──────── */
    /* Walk the extracted tree looking for:
     *   - Paths containing ".."
     *   - Symlinks pointing outside dest_dir
     *   - Absolute paths in the archive
     */
    char scan_cmd[512];
    snprintf(scan_cmd, sizeof(scan_cmd),
        "cd '%s' && ("
        "  find . -name '*..*' -o -path '*/../*' | grep -q . && "
        "    echo 'PATH_TRAVERSAL' && exit 1; "
        "  find . -type l | while read lnk; do "
        "    target=$(readlink -f \"$lnk\" 2>/dev/null); "
        "    case \"$target\" in '%s'*) ;; *) "
        "      echo \"SYMLINK_ESCAPE: $lnk -> $target\"; exit 1;; esac; "
        "  done"
        ")",
        dest_dir, dest_dir);

    rc = system(scan_cmd);
    if (rc != 0) {
        fprintf(stderr, "[init] SECURITY: payload failed path safety scan\n");
        /* Nuke the extracted files */
        char nuke[256];
        snprintf(nuke, sizeof(nuke), "rm -rf '%s'/*", dest_dir);
        system(nuke);
        return -1;
    }

    puts("  Extraction: OK (path safety verified)");
    return 0;
}

/* ── Boot facts ──────────────────────────────────────────────────────────── */

static void write_boot_facts(int verdict, const struct cart_header *hdr,
                              const char *cart_path)
{
    /* Mount tmpfs for /run if not already mounted */
    mkdir("/run", 0755);
    mount("tmpfs", "/run", "tmpfs", 0, "size=1m,mode=0755");

    FILE *f = fopen("/run/boot-facts", "w");
    if (!f) {
        fprintf(stderr, "[init] cannot write /run/boot-facts\n");
        return;
    }

    const char *verdict_str;
    switch (verdict) {
        case CART_VERDICT_VERIFIED:   verdict_str = "verified"; break;
        case CART_VERDICT_UNSIGNED:   verdict_str = "unsigned"; break;
        case CART_VERDICT_BAD_SIG:    verdict_str = "bad_sig"; break;
        case CART_VERDICT_BAD_HASH:   verdict_str = "bad_hash"; break;
        case CART_VERDICT_BAD_FORMAT: verdict_str = "bad_format"; break;
        case CART_VERDICT_NO_CART:    verdict_str = "no_cart"; break;
        default:                      verdict_str = "unknown"; break;
    }

    char manifest_hex[SHA512_DIGEST_SIZE * 2 + 1];
    char payload_hex[SHA512_DIGEST_SIZE * 2 + 1];
    char key_id_hex[CART_KEY_ID_SIZE * 2 + 1];
    char pubkey_hex[CART_PUBKEY_SIZE * 2 + 1];

    if (hdr) {
        hex_encode(hdr->manifest_hash, SHA512_DIGEST_SIZE, manifest_hex);
        hex_encode(hdr->payload_hash, SHA512_DIGEST_SIZE, payload_hex);
        hex_encode(hdr->key_id, CART_KEY_ID_SIZE, key_id_hex);
    } else {
        strcpy(manifest_hex, "none");
        strcpy(payload_hex, "none");
        strcpy(key_id_hex, "none");
    }
    hex_encode(OS_PUBKEY, CART_PUBKEY_SIZE, pubkey_hex);

    time_t now = time(NULL);

    fprintf(f, "verdict=%s\n", verdict_str);
    fprintf(f, "manifest_hash=%s\n", manifest_hex);
    fprintf(f, "payload_hash=%s\n", payload_hex);
    fprintf(f, "key_id=%s\n", key_id_hex);
    fprintf(f, "pubkey=%s\n", pubkey_hex);
    fprintf(f, "boot_time=%lld\n", (long long)now);
    fprintf(f, "cart_path=%s\n", cart_path ? cart_path : "none");

    /* Pre-read system info that the sandbox may not be able to access
     * (mount namespace hides /proc when sysmon: false) */
    {
        FILE *vf = fopen("/proc/version", "r");
        if (vf) {
            char line[512];
            if (fgets(line, sizeof(line), vf)) {
                /* Extract "Linux version X.Y.Z" → just the version token */
                char *tok = strtok(line, " ");  /* "Linux" */
                tok = strtok(NULL, " ");         /* "version" */
                tok = strtok(NULL, " ");         /* "X.Y.Z-something" */
                if (tok) fprintf(f, "kernel=%s\n", tok);
            }
            fclose(vf);
        }

        FILE *uf = fopen("/proc/uptime", "r");
        if (uf) {
            char ubuf[64];
            if (fgets(ubuf, sizeof(ubuf), uf)) {
                char *sp = strchr(ubuf, ' ');
                if (sp) *sp = '\0';
                char *nl = strchr(ubuf, '\n');
                if (nl) *nl = '\0';
                fprintf(f, "uptime=%s\n", ubuf);
            }
            fclose(uf);
        }

        /* DRI devices */
        DIR *dri = opendir("/dev/dri");
        if (dri) {
            struct dirent *de;
            int first = 1;
            fprintf(f, "dri=");
            while ((de = readdir(dri)) != NULL) {
                if (de->d_name[0] == '.') continue;
                fprintf(f, "%s%s", first ? "" : "  ", de->d_name);
                first = 0;
            }
            fprintf(f, "\n");
            closedir(dri);
        }
    }

    fclose(f);

    chmod("/run/boot-facts", 0444);
    puts("  Boot facts written to /run/boot-facts");
}

/* ── Layer 2: Namespace isolation ─────────────────────────────────────────
 * Replace fork() with clone() using capability-derived namespace flags.
 * Even if the cart bypasses the Lua sandbox via FFI, the kernel denies
 * the syscalls (no network interface, no visible files, no other PIDs).
 */

/* musl doesn't wrap pivot_root — call it directly */
static int pivot_root(const char *new_root, const char *put_old) {
    return (int)syscall(SYS_pivot_root, new_root, put_old);
}

struct child_args {
    int verdict_fd;      /* read end of verdict pipe (becomes FD 3) */
    int do_mount_ns;     /* set up restricted mount tree */
    int do_proc;         /* mount /proc inside the namespace */
};

/* Bind-mount helper: creates the mount point and bind-mounts src → dst.
 * If read_only is set, remounts MS_RDONLY after the initial bind. */
static void bind_mount(const char *src, const char *dst, int read_only) {
    struct stat st;
    if (stat(src, &st) != 0) return;    /* source doesn't exist, skip */

    if (S_ISDIR(st.st_mode)) {
        mkdir(dst, 0755);
    } else {
        /* file bind mount: create empty file as mount point */
        int fd = open(dst, O_CREAT | O_WRONLY, 0644);
        if (fd >= 0) close(fd);
    }

    mount(src, dst, NULL, MS_BIND, NULL);
    if (read_only)
        mount(NULL, dst, NULL, MS_BIND | MS_REMOUNT | MS_RDONLY, NULL);
}

/* Build a minimal mount tree inside /mnt (a tmpfs), then pivot_root into it.
 * The cart can only see: /app, /os, /usr/lib, /usr/bin, /usr/share/fonts,
 * /lib, /dev/dri, /dev/console, /tmp, and optionally /proc. */
static void setup_restricted_mounts(int mount_proc) {
    fprintf(stderr, "[ns] setting up restricted mount tree\n");

    /* Make all existing mounts private so changes don't leak to parent */
    if (mount("", "/", "", MS_REC | MS_PRIVATE, NULL) != 0)
        fprintf(stderr, "[ns] MS_PRIVATE on /: %s\n", strerror(errno));

    /* Create tmpfs as the new root at /mnt */
    mkdir("/mnt", 0755);
    if (mount("tmpfs", "/mnt", "tmpfs", 0, "size=128m,mode=0755") != 0) {
        fprintf(stderr, "[ns] mount tmpfs /mnt failed: %s\n", strerror(errno));
        return;
    }

    /* Create directory structure inside new root */
    mkdir("/mnt/app", 0755);
    mkdir("/mnt/os", 0755);
    mkdir("/mnt/usr", 0755);
    mkdir("/mnt/usr/lib", 0755);
    mkdir("/mnt/usr/bin", 0755);
    mkdir("/mnt/usr/share", 0755);
    mkdir("/mnt/usr/share/fonts", 0755);
    mkdir("/mnt/lib", 0755);
    mkdir("/mnt/dev", 0755);
    mkdir("/mnt/dev/dri", 0755);
    mkdir("/mnt/dev/input", 0755);
    mkdir("/mnt/tmp", 01777);
    mkdir("/mnt/run", 0755);

    /* Bind mount only what the cart needs */
    bind_mount("/app",              "/mnt/app",              1);  /* cart code (RO) */
    bind_mount("/os",               "/mnt/os",               1);  /* sandbox.lua (RO) */
    bind_mount("/usr/lib",          "/mnt/usr/lib",          1);  /* SDL2, Mesa, musl (RO) */
    bind_mount("/usr/bin",          "/mnt/usr/bin",          1);  /* luajit (RO) */
    bind_mount("/usr/share/fonts",  "/mnt/usr/share/fonts",  1);  /* fonts (RO) */
    bind_mount("/lib",              "/mnt/lib",              1);  /* musl dynamic linker (RO) */
    bind_mount("/dev/dri",          "/mnt/dev/dri",          0);  /* GPU device nodes (RW) */
    bind_mount("/dev/console",      "/mnt/dev/console",      0);  /* stdout/stderr */
    bind_mount("/dev/null",         "/mnt/dev/null",         0);  /* /dev/null */
    bind_mount("/dev/zero",         "/mnt/dev/zero",         0);  /* /dev/zero */
    bind_mount("/dev/urandom",      "/mnt/dev/urandom",      0);  /* /dev/urandom */
    bind_mount("/dev/input",        "/mnt/dev/input",        0);  /* evdev input devices */
    bind_mount("/run/boot-facts",   "/mnt/run/boot-facts",   1);  /* attestation (RO) */

    /* /sys is needed for Mesa's GPU device enumeration (reads /sys/dev/char/,
     * /sys/class/drm/, /sys/bus/pci/). Without it, Mesa can't load the DRI driver.
     * This is read-only and exposes hardware topology but not process info. */
    mkdir("/mnt/sys", 0555);
    mount("sysfs", "/mnt/sys", "sysfs", MS_RDONLY, NULL);

    /* /proc only if sysmon capability is granted */
    if (mount_proc) {
        mkdir("/mnt/proc", 0555);
        mount("proc", "/mnt/proc", "proc", 0, NULL);
    }

    /* /tmp as fresh tmpfs for scratch space */
    mount("tmpfs", "/mnt/tmp", "tmpfs", 0, "size=32m,mode=1777");

    fprintf(stderr, "[ns] bind mounts done, attempting pivot_root\n");

    /* pivot_root: swap /mnt → / and move old root to /mnt/oldroot */
    mkdir("/mnt/oldroot", 0755);
    if (pivot_root("/mnt", "/mnt/oldroot") != 0) {
        /* Fallback to chroot if pivot_root fails (e.g. initramfs) */
        fprintf(stderr, "[ns] pivot_root failed (%s), falling back to chroot\n",
                strerror(errno));
        if (chroot("/mnt") != 0) {
            fprintf(stderr, "[ns] chroot also failed: %s\n", strerror(errno));
            return;
        }
        chdir("/");
        fprintf(stderr, "[ns] chroot to /mnt OK\n");
        return;
    }

    /* Detach the old root and remove the mount point */
    umount2("/oldroot", MNT_DETACH);
    rmdir("/oldroot");
    chdir("/");
    fprintf(stderr, "[ns] pivot_root OK\n");
}

#define CHILD_STACK_SIZE (1024 * 1024)  /* 1 MB */

static int child_fn(void *arg) {
    struct child_args *ca = (struct child_args *)arg;

    /* Set up FD 3 as verdict pipe read end */
    if (ca->verdict_fd >= 0) {
        dup2(ca->verdict_fd, 3);
        if (ca->verdict_fd != 3)
            close(ca->verdict_fd);
    }

    /* Mount namespace setup (if CLONE_NEWNS was used) */
    if (ca->do_mount_ns) {
        fprintf(stderr, "[child] setting up mount namespace\n");
        setup_restricted_mounts(ca->do_proc);
        fprintf(stderr, "[child] mount namespace ready\n");
    }

    fprintf(stderr, "[child] execv /usr/bin/luajit /os/sandbox.lua\n");

    /* sandbox.lua lives in the OS rootfs, NOT in the cart.
     * The cart cannot replace or modify the jailer. */
    char *argv[] = { "/usr/bin/luajit", "/os/sandbox.lua", NULL };
    execv("/usr/bin/luajit", argv);
    fprintf(stderr, "[child] execv luajit failed: %s\n", strerror(errno));
    _exit(1);
}

/* ── Main ────────────────────────────────────────────────────────────────── */

int main(void) {
    /* ── Filesystems ──────────────────────────────────────────────────────── */
    mount("proc",     "/proc", "proc",     0, NULL);
    mount("sysfs",    "/sys",  "sysfs",    0, NULL);
    mount("devtmpfs", "/dev",  "devtmpfs", 0, NULL);

    int pfd = open("/proc/sys/kernel/printk", O_WRONLY);
    if (pfd >= 0) { write(pfd, "1\n", 2); close(pfd); }

    int con = open("/dev/console", O_RDWR);
    if (con >= 0) { dup2(con, 0); dup2(con, 1); dup2(con, 2); if (con > 2) close(con); }

    /* ── Install busybox applets ──────────────────────────────────────────── */
    char *bb[] = { "/bin/busybox", "--install", "-s", "/bin", NULL };
    run_wait(bb);

    /* ── Banner ───────────────────────────────────────────────────────────── */
    puts("\n  CartridgeOS v0.1");
    puts("  ReactJIT  --  no X11, no Wayland, no display server");

    char ver[64] = "unknown";
    FILE *vf = fopen("/proc/version", "r");
    if (vf) {
        char line[512]; char *tok;
        if (fgets(line, sizeof(line), vf)) {
            tok = strtok(line, " "); tok = strtok(NULL, " "); tok = strtok(NULL, " ");
            if (tok) strncpy(ver, tok, sizeof(ver)-1);
        }
        fclose(vf);
    }
    printf("  kernel: %s\n\n", ver);

    /* ── Load virtio-gpu module ────────────────────────────────────────────── */
    puts("  Loading virtio-gpu driver...");
    fflush(stdout);
    char *modargs[] = { "/bin/busybox", "modprobe", "virtio-gpu", NULL };
    run_wait(modargs);
    usleep(1500000);

    if (access("/dev/dri/card0", F_OK) == 0)
        puts("  DRM: /dev/dri/card0 ready");
    else
        puts("  WARNING: /dev/dri/card0 missing");
    if (access("/dev/dri/renderD128", F_OK) == 0)
        puts("  DRM: /dev/dri/renderD128 ready");
    else
        puts("  DRM: /dev/dri/renderD128 not found");

    /* Sanity check: GPU PCI */
    puts("  GPU PCI:");
    char *gpucheck[] = { "/bin/sh", "-c",
        "for d in /sys/bus/pci/devices/*; do "
        "  if [ -f \"$d/class\" ] && grep -q '^0x030' \"$d/class\"; then "
        "    printf '    %s vendor=%s device=%s\\n' "
        "      \"$(basename $d)\" \"$(cat $d/vendor)\" \"$(cat $d/device)\"; "
        "  fi; "
        "done", NULL };
    run_wait(gpucheck);
    fflush(stdout);

    /* ── Environment ──────────────────────────────────────────────────────── */
    setenv("SDL_VIDEODRIVER",    "kmsdrm",            1);
    setenv("LD_LIBRARY_PATH",    "/app:/usr/lib:/lib", 1);
    setenv("LIBGL_DRIVERS_PATH", "/usr/lib/xorg/modules/dri", 1);
    setenv("LIBGL_DRIVERS_DIR",  "/usr/lib/xorg/modules/dri", 1);
    setenv("EGL_PLATFORM",       "gbm",               1);
    setenv("MESA_EGL_NO_X11",    "1",                 1);
    setenv("MESA_LOADER_DRIVER_OVERRIDE", "virtio_gpu", 1);
    setenv("LD_PRELOAD",  "/app/gbm_format_shim.so",  1);

    /* ── Cart verification ────────────────────────────────────────────────── */
    puts("\n  === Cart Verification ===");

    int dev_mode = cmdline_has("cart_dev=1");
    char cart_path[256] = "/boot/app.cart";
    cmdline_get("cart", cart_path, sizeof(cart_path));

    int verdict = CART_VERDICT_NO_CART;
    struct cart_header hdr;
    memset(&hdr, 0, sizeof(hdr));

    if (access(cart_path, F_OK) == 0) {
        printf("  Cart: %s\n", cart_path);
        verdict = verify_cart(cart_path, &hdr);

        if (verdict == CART_VERDICT_VERIFIED) {
            puts("  Status: VERIFIED — extracting...");

            /* Mount fresh tmpfs for /app (overwrite initrd contents) */
            mount("tmpfs", "/app", "tmpfs", 0, "size=64m,mode=0755");

            if (extract_payload(cart_path, hdr.manifest_len,
                                hdr.payload_len, "/app") != 0) {
                fprintf(stderr, "[init] extraction failed — aborting\n");
                verdict = CART_VERDICT_BAD_FORMAT;
            } else {
                /* Write manifest.json for the boot screen to read */
                char manifest_path[256];
                snprintf(manifest_path, sizeof(manifest_path),
                         "%s/manifest.json", "/app");

                /* Re-read manifest from cart and write to /app */
                FILE *cf = fopen(cart_path, "rb");
                if (cf) {
                    fseek(cf, CART_HEADER_SIZE, SEEK_SET);
                    FILE *mf = fopen(manifest_path, "w");
                    if (mf) {
                        uint8_t buf[4096];
                        uint32_t rem = hdr.manifest_len;
                        while (rem > 0) {
                            size_t n = rem < sizeof(buf) ? rem : sizeof(buf);
                            size_t got = fread(buf, 1, n, cf);
                            if (got == 0) break;
                            fwrite(buf, 1, got, mf);
                            rem -= (uint32_t)got;
                        }
                        fclose(mf);
                    }
                    fclose(cf);
                }
            }
        } else {
            printf("  Status: FAILED (verdict=%d)\n", verdict);
            fprintf(stderr, "[init] cart verification failed — will not extract\n");
        }
    } else {
        printf("  Cart: %s NOT FOUND\n", cart_path);

        if (dev_mode) {
            puts("  Dev mode (cart_dev=1): running unsigned from initrd");
            verdict = CART_VERDICT_UNSIGNED;
        } else {
            fprintf(stderr,
                "[init] No .cart found and cart_dev=1 not set.\n"
                "       Add 'cart_dev=1' to kernel cmdline for unsigned dev mode.\n");
            verdict = CART_VERDICT_NO_CART;
        }
    }

    /* ── Write boot facts ────────────────────────────────────────────────── */
    write_boot_facts(verdict,
                     verdict == CART_VERDICT_VERIFIED ? &hdr : NULL,
                     cart_path);

    /* ── Create verdict pipe ─────────────────────────────────────────────── */
    int verdict_pipe[2] = {-1, -1};
    if (pipe(verdict_pipe) != 0) {
        fprintf(stderr, "[init] pipe() failed: %s\n", strerror(errno));
    }

    struct cart_verdict vdict;
    vdict.code = (uint8_t)verdict;
    memset(vdict.key_id, 0, CART_KEY_ID_SIZE);
    if (verdict == CART_VERDICT_VERIFIED)
        memcpy(vdict.key_id, hdr.key_id, CART_KEY_ID_SIZE);
    vdict.boot_time = (uint64_t)time(NULL);

    if (verdict_pipe[1] >= 0) {
        write(verdict_pipe[1], &vdict, sizeof(vdict));
        close(verdict_pipe[1]);
    }

    /* ── Load input modules (gated on manifest capabilities) ────────────── */
    /* Read /app/manifest.json and check for keyboard/mouse/usb caps.
     * Uses simple string search — cartridge-pack.py produces canonical JSON. */
    int has_keyboard = 0, has_mouse = 0, has_usb = 0;
    int has_network = 0, has_filesystem = 0, has_process = 0, has_sysmon = 0;
    {
        FILE *mf = fopen("/app/manifest.json", "r");
        if (mf) {
            char mbuf[4096];
            size_t mlen = fread(mbuf, 1, sizeof(mbuf) - 1, mf);
            fclose(mf);
            mbuf[mlen] = '\0';
            if (strstr(mbuf, "\"keyboard\":true") || strstr(mbuf, "\"keyboard\": true"))
                has_keyboard = 1;
            if (strstr(mbuf, "\"mouse\":true") || strstr(mbuf, "\"mouse\": true"))
                has_mouse = 1;
            if (strstr(mbuf, "\"usb\":true") || strstr(mbuf, "\"usb\": true"))
                has_usb = 1;
            if (strstr(mbuf, "\"network\":true") || strstr(mbuf, "\"network\": true"))
                has_network = 1;
            if (strstr(mbuf, "\"filesystem\":true") || strstr(mbuf, "\"filesystem\": true"))
                has_filesystem = 1;
            if (strstr(mbuf, "\"process\":true") || strstr(mbuf, "\"process\": true"))
                has_process = 1;
            if (strstr(mbuf, "\"sysmon\":true") || strstr(mbuf, "\"sysmon\": true"))
                has_sysmon = 1;
        }

        if (has_keyboard || has_mouse) {
            puts("  Loading input modules (keyboard/mouse)...");
            char *input_mods[] = {
                "hid", "hid-generic", "evdev", "mousedev", "psmouse",
                "virtio_input", NULL
            };
            for (int i = 0; input_mods[i]; i++) {
                char *args[] = { "/bin/busybox", "modprobe", input_mods[i], NULL };
                run_wait(args);
            }
        }

        if (has_usb) {
            puts("  Loading USB host controller modules...");
            char *usb_mods[] = {
                "usb-common", "usbcore", "xhci-hcd", "xhci-pci",
                "ehci-hcd", "ehci-pci", "uhci-hcd", "ohci-hcd", "ohci-pci",
                "usbhid", NULL
            };
            for (int i = 0; usb_mods[i]; i++) {
                char *args[] = { "/bin/busybox", "modprobe", usb_mods[i], NULL };
                run_wait(args);
            }
        }

        if (has_keyboard || has_mouse || has_usb) {
            usleep(300000); /* let kernel enumerate devices */
        }
        fflush(stdout);
    }

    /* ── Launch luajit ───────────────────────────────────────────────────── */
    /* Only launch if verified OR dev mode unsigned */
    if (verdict != CART_VERDICT_VERIFIED && verdict != CART_VERDICT_UNSIGNED) {
        printf("\n  [init] Cart not verified. Dropping to shell.\n");
        fflush(stdout);
        goto shell;
    }

    /* ── Build namespace flags from manifest capabilities ────────────── */
    int clone_flags = SIGCHLD;
    if (!has_network)    clone_flags |= CLONE_NEWNET;
    if (!has_filesystem) clone_flags |= CLONE_NEWNS;
    if (!has_process)    clone_flags |= CLONE_NEWPID;

    /* Log active namespaces */
    {
        const char *ns_names[3];
        int ns_count = 0;
        if (!has_network)    ns_names[ns_count++] = "net";
        if (!has_filesystem) ns_names[ns_count++] = "mnt";
        if (!has_process)    ns_names[ns_count++] = "pid";
        if (ns_count > 0) {
            printf("  Namespaces: ");
            for (int i = 0; i < ns_count; i++)
                printf("%s%s", ns_names[i], i < ns_count - 1 ? ", " : "");
            printf("\n");
        } else {
            printf("  Namespaces: none (all capabilities granted)\n");
        }
    }

    /* Append namespace state to boot-facts (written earlier, before manifest parse) */
    {
        FILE *bf = fopen("/run/boot-facts", "a");
        if (bf) {
            fprintf(bf, "namespaces=");
            int first = 1;
            if (!has_network)    { fprintf(bf, "%snet", first ? "" : ","); first = 0; }
            if (!has_filesystem) { fprintf(bf, "%smnt", first ? "" : ","); first = 0; }
            if (!has_process)    { fprintf(bf, "%spid", first ? "" : ","); first = 0; }
            if (first) fprintf(bf, "none");
            fprintf(bf, "\n");
            fclose(bf);
            chmod("/run/boot-facts", 0444);
        }
    }

    printf("\n  Launching ReactJIT (%s)...\n\n",
           verdict == CART_VERDICT_VERIFIED ? "verified" : "unsigned/dev");
    fflush(stdout);

    /* Allocate child stack for clone() */
    char *child_stack = (char *)mmap(NULL, CHILD_STACK_SIZE,
                                      PROT_READ | PROT_WRITE,
                                      MAP_PRIVATE | MAP_ANONYMOUS | MAP_STACK,
                                      -1, 0);
    if (child_stack == MAP_FAILED) {
        fprintf(stderr, "[init] mmap child stack failed: %s\n", strerror(errno));
        goto shell;
    }
    char *stack_top = child_stack + CHILD_STACK_SIZE;

    struct child_args cargs;
    cargs.verdict_fd  = verdict_pipe[0];
    cargs.do_mount_ns = !has_filesystem;
    cargs.do_proc     = has_sysmon;

    pid_t app = clone(child_fn, stack_top, clone_flags, &cargs);

    /* parent: close read end of verdict pipe */
    if (verdict_pipe[0] >= 0)
        close(verdict_pipe[0]);

    if (app > 0) {
        int status;
        waitpid(app, &status, 0);
        if (WIFEXITED(status))
            printf("\n  [init] app exited (code %d)\n", WEXITSTATUS(status));
        else if (WIFSIGNALED(status))
            printf("\n  [init] app killed by signal %d\n", WTERMSIG(status));
        else
            printf("\n  [init] app stopped (raw status %d)\n", status);
    } else {
        fprintf(stderr, "[init] clone failed: %s\n", strerror(errno));
    }

    munmap(child_stack, CHILD_STACK_SIZE);

shell:
    /* ── Fallback shell ───────────────────────────────────────────────────── */
    puts("\n  [init] dropping to shell (Ctrl-D to reboot)\n");
    fflush(stdout);
    char *sh[] = { "/bin/sh", NULL };
    execv("/bin/sh", sh);

    /* PID 1 must not exit */
    while (1) sleep(1);
    return 0;
}
