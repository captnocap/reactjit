/*
 * CartridgeOS init.c — PID 1
 *
 * Static musl binary. Verify-first .cart loading:
 *   1. Mount filesystems, load virtio-gpu
 *   2. Find .cart (kernel cmdline or default path)
 *   3. Ed25519 verify signature over header (contains hashes)
 *   4. Stream manifest + payload, verify SHA-512 hashes
 *   5. Extract payload cpio to fresh /app tmpfs (secure: no path traversal)
 *   6. Write /run/boot-facts (read-only attestation)
 *   7. Create verdict pipe (FD 3) — binary, unforgeable
 *   8. Fork luajit
 *
 * Dev mode: if cart_dev=1 on kernel cmdline AND no .cart found,
 * falls back to running /app/main.lua from initrd. Otherwise hard fail.
 *
 * Dependencies: tweetnacl.c (Ed25519), sha512.c (streaming hash)
 */

