--[[
  game_server/config.lua — Config resolution and game-specific file generation

  Handles:
  1. Resolving config from JSON file path or Lua table
  2. Generating server.properties (Minecraft)
  3. Generating server.cfg (Source/Source2) + mapcycle.txt
  4. Generating autoexec.cfg (GoldSrc) + mapcycle.txt
  5. Config diffing and hot-apply via RCON
]]

local json = require("lua.json")

local Config = {}

-- ── Config resolution ───────────────────────────────────────────────────────

--- Resolve config from string path or table.
--- @param config string|table  JSON file path or inline table
--- @return table
function Config.resolve(config)
  if type(config) == "table" then
    return config
  end

  if type(config) == "string" then
    local f, err = io.open(config, "r")
    if not f then
      error("Cannot read config file: " .. config .. " (" .. tostring(err) .. ")")
    end
    local content = f:read("*all")
    f:close()

    local ok, decoded = pcall(json.decode, content)
    if not ok then
      error("Invalid JSON in config file: " .. config .. " (" .. tostring(decoded) .. ")")
    end
    return decoded
  end

  return {}
end

-- ── Minecraft: server.properties ────────────────────────────────────────────

-- Map from our config keys to Minecraft server.properties keys
local MC_KEY_MAP = {
  port            = "server-port",
  maxPlayers      = "max-players",
  name            = "motd",
  map             = "level-name",
  difficulty      = "difficulty",
  gameType        = "gamemode",
  onlineMode      = "online-mode",
  pvp             = "pvp",
  viewDistance     = "view-distance",
  spawnProtection = "spawn-protection",
  maxBuildHeight  = "max-build-height",
  allowNether     = "allow-nether",
  allowFlight     = "allow-flight",
  password        = "rcon.password",
  rconPassword    = "rcon.password",
  rconPort        = "rcon.port",
}

local MC_DEFAULTS = {
  ["server-port"]      = 25565,
  ["max-players"]      = 20,
  ["motd"]             = "A ReactJIT Minecraft Server",
  ["level-name"]       = "world",
  ["difficulty"]       = "normal",
  ["gamemode"]         = "survival",
  ["online-mode"]      = true,
  ["pvp"]              = true,
  ["view-distance"]    = 10,
  ["spawn-protection"] = 16,
  ["max-build-height"] = 256,
  ["allow-nether"]     = true,
  ["allow-flight"]     = false,
  ["enable-rcon"]      = false,
  ["rcon.password"]    = "",
  ["rcon.port"]        = 25575,
  ["enable-query"]     = false,
}

function Config.generateMinecraft(config, outputPath)
  local props = {}

  -- Start with defaults
  for k, v in pairs(MC_DEFAULTS) do
    props[k] = v
  end

  -- Apply config overrides
  for key, value in pairs(config) do
    local mcKey = MC_KEY_MAP[key]
    if mcKey then
      props[mcKey] = value
    end
  end

  -- Enable RCON if password is set
  if config.rconPassword and config.rconPassword ~= "" then
    props["enable-rcon"] = true
    props["rcon.password"] = config.rconPassword
  elseif config.password and config.password ~= "" then
    props["enable-rcon"] = true
    props["rcon.password"] = config.password
  end

  -- Write server.properties
  local f, err = io.open(outputPath, "w")
  if not f then return nil, err end

  f:write("# Minecraft server.properties — generated by ReactJIT GameServer\n")
  f:write("# " .. os.date() .. "\n\n")

  -- Sort keys for deterministic output
  local keys = {}
  for k in pairs(props) do keys[#keys + 1] = k end
  table.sort(keys)

  for _, k in ipairs(keys) do
    local v = props[k]
    if type(v) == "boolean" then
      f:write(k .. "=" .. (v and "true" or "false") .. "\n")
    else
      f:write(k .. "=" .. tostring(v) .. "\n")
    end
  end

  -- EULA acceptance (required by Minecraft)
  f:close()

  -- Also write eula.txt
  local eulaPath = outputPath:match("(.+)/[^/]+$") or "."
  local ef = io.open(eulaPath .. "/eula.txt", "w")
  if ef then
    ef:write("# By using ReactJIT GameServer, you accept the Minecraft EULA\n")
    ef:write("# https://account.mojang.com/documents/minecraft_eula\n")
    ef:write("eula=true\n")
    ef:close()
  end

  return true
end

-- ── Source / Source2 / GoldSrc: server.cfg ───────────────────────────────────

-- Map from our config keys to Source engine cvars
local SOURCE_KEY_MAP = {
  name          = "hostname",
  password      = "sv_password",
  rconPassword  = "rcon_password",
  maxPlayers    = "maxplayers",
  tickrate      = "sv_maxrate",
}

function Config.generateSource(config, outputPath)
  local lines = {}
  lines[#lines + 1] = '// server.cfg — generated by ReactJIT GameServer'
  lines[#lines + 1] = '// ' .. os.date()
  lines[#lines + 1] = ''

  -- Map known keys
  local hostname = config.name or "ReactJIT Game Server"
  lines[#lines + 1] = string.format('hostname "%s"', hostname)

  if config.password and config.password ~= "" then
    lines[#lines + 1] = string.format('sv_password "%s"', config.password)
  end

  if config.rconPassword and config.rconPassword ~= "" then
    lines[#lines + 1] = string.format('rcon_password "%s"', config.rconPassword)
  end

  if config.maxPlayers then
    lines[#lines + 1] = string.format('maxplayers %d', config.maxPlayers)
  end

  if config.tickrate then
    lines[#lines + 1] = string.format('sv_maxrate %d', config.tickrate)
  end

  lines[#lines + 1] = ''
  lines[#lines + 1] = '// Game settings'

  if config.gamemode then
    lines[#lines + 1] = string.format('game_mode %s', config.gamemode)
  end

  -- Passthrough: any key not in our known list goes as a raw cvar
  local known = { name=1, password=1, rconPassword=1, rconPort=1, maxPlayers=1,
                   tickrate=1, game=1, gamemode=1, port=1, map=1, mapRotation=1,
                   type=1, serverPath=1, steamcmdPath=1, appId=1 }
  for key, value in pairs(config) do
    if not known[key] then
      if type(value) == "string" then
        lines[#lines + 1] = string.format('%s "%s"', key, value)
      elseif type(value) == "number" then
        lines[#lines + 1] = string.format('%s %s', key, tostring(value))
      elseif type(value) == "boolean" then
        lines[#lines + 1] = string.format('%s %d', key, value and 1 or 0)
      end
    end
  end

  -- Write server.cfg
  local f, err = io.open(outputPath, "w")
  if not f then return nil, err end
  f:write(table.concat(lines, "\n") .. "\n")
  f:close()

  -- Write mapcycle.txt if mapRotation is specified
  if config.mapRotation and #config.mapRotation > 0 then
    local dir = outputPath:match("(.+)/[^/]+$") or "."
    local mf = io.open(dir .. "/mapcycle.txt", "w")
    if mf then
      mf:write("// Map rotation — generated by ReactJIT GameServer\n")
      for _, map in ipairs(config.mapRotation) do
        mf:write(map .. "\n")
      end
      mf:close()
    end
  end

  return true
end

-- ── Config diff ─────────────────────────────────────────────────────────────

--- Compare two config tables, return list of changed keys.
--- @param old table
--- @param new table
--- @return table  Array of { key, oldVal, newVal }
function Config.diff(old, new)
  local changes = {}
  if not old or not new then return changes end

  -- Check keys in new
  for k, v in pairs(new) do
    if old[k] ~= v then
      changes[#changes + 1] = { key = k, oldVal = old[k], newVal = v }
    end
  end

  -- Check removed keys
  for k, v in pairs(old) do
    if new[k] == nil then
      changes[#changes + 1] = { key = k, oldVal = v, newVal = nil }
    end
  end

  return changes
end

-- ── Hot-apply via RCON (no restart needed) ──────────────────────────────────

--- Generate RCON commands for live Source/Source2/GoldSrc config changes.
--- @param changes table  Array of { key, oldVal, newVal }
--- @return table  Array of RCON command strings
function Config.hotApplySource(changes)
  local cmds = {}
  for _, ch in ipairs(changes) do
    local k, v = ch.key, ch.newVal
    if v == nil then goto continue end

    local cvar = SOURCE_KEY_MAP[k]
    if cvar then
      if type(v) == "string" then
        cmds[#cmds + 1] = string.format('%s "%s"', cvar, v)
      else
        cmds[#cmds + 1] = string.format('%s %s', cvar, tostring(v))
      end
    elseif k == "map" then
      cmds[#cmds + 1] = string.format('changelevel %s', v)
    end

    ::continue::
  end
  return cmds
end

--- Generate RCON commands for live Minecraft config changes.
--- @param changes table
--- @return table
function Config.hotApplyMinecraft(changes)
  local cmds = {}
  for _, ch in ipairs(changes) do
    local k, v = ch.key, ch.newVal
    if v == nil then goto continue end

    if k == "difficulty" then
      cmds[#cmds + 1] = "difficulty " .. tostring(v)
    elseif k == "gameType" then
      cmds[#cmds + 1] = "defaultgamemode " .. tostring(v)
    elseif k == "map" then
      -- Minecraft doesn't hot-switch worlds via RCON easily
    elseif k == "pvp" then
      cmds[#cmds + 1] = "gamerule pvp " .. (v and "true" or "false")
    end

    ::continue::
  end
  return cmds
end

return Config
