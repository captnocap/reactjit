#!/usr/bin/env bun
// Rasterize a curated set of Lobe brand SVGs to PNG, then emit a TS data
// module that inlines each PNG as a base64 data URL. ModelCard renders via
// <Image source={...}> — avoids the Graph.Path fill rasterizer entirely, so
// brand logos land pixel-perfect including gradients and complex silhouettes.
//
// Requires ImageMagick (`convert`) on PATH. Lobe icons ship as
// @lobehub/icons-static-svg in node_modules.
//
// Run: bun scripts/build-provider-icons.mjs

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { Resvg } from '@resvg/resvg-js';

const repoRoot = resolve(import.meta.dirname, '..');
const iconsDir = resolve(repoRoot, 'node_modules/@lobehub/icons-static-svg/icons');
const outFile = resolve(repoRoot, 'cart/component-gallery/components/model-card/providerIcons.generated.ts');

// Rasterize at 128px — handles up-to-64px display sizes cleanly with a little
// headroom. Bigger wastes bundle bytes; smaller pixelates when Image scales up.
const RASTER_PX = 128;

const PROVIDERS = [
  { id: 'openai', file: 'openai.svg' },
  { id: 'anthropic', file: 'anthropic.svg' },
  { id: 'claude', file: 'claude-color.svg' },
  { id: 'gemini', file: 'gemini-color.svg' },
  { id: 'google', file: 'google-color.svg' },
  { id: 'deepseek', file: 'deepseek-color.svg' },
  { id: 'mistral', file: 'mistral-color.svg' },
  { id: 'cohere', file: 'cohere-color.svg' },
  { id: 'grok', file: 'grok.svg' },
  { id: 'groq', file: 'groq.svg' },
  { id: 'meta', file: 'meta-color.svg' },
  { id: 'metaai', file: 'metaai-color.svg' },
  { id: 'ollama', file: 'ollama.svg' },
  { id: 'perplexity', file: 'perplexity-color.svg' },
  { id: 'xai', file: 'xai.svg' },
  { id: 'qwen', file: 'qwen-color.svg' },
  { id: 'huggingface', file: 'huggingface-color.svg' },
  { id: 'zhipu', file: 'zhipu-color.svg' },
];

function rasterize(svgPath) {
  // resvg (Rust/WASM) — the Lobe SVGs use linear gradients + multi-path stacks
  // and need a real SVG renderer. `fitTo.mode: 'width'` rescales to RASTER_PX
  // preserving the source viewBox's aspect ratio (all Lobe icons are 1:1).
  const svg = readFileSync(svgPath);
  const resvg = new Resvg(svg, {
    fitTo: { mode: 'width', value: RASTER_PX },
    background: 'rgba(0,0,0,0)',
  });
  return resvg.render().asPng();
}

const providers = {};
for (const { id, file } of PROVIDERS) {
  try {
    const png = rasterize(resolve(iconsDir, file));
    providers[id] = `data:image/png;base64,${png.toString('base64')}`;
  } catch (e) {
    console.warn(`[provider-icons] skipped ${id}: ${e.message.split('\n')[0]}`);
  }
}

const entries = Object.entries(providers)
  .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)}`)
  .join(',\n');

const header = `// AUTO-GENERATED by scripts/build-provider-icons.mjs — do not edit by hand.
// Source: @lobehub/icons-static-svg (MIT), rasterized to ${RASTER_PX}px PNG via ImageMagick.
// Re-run the script to refresh.

export const PROVIDER_ICONS: Record<string, string> = {
${entries}
};

export type ProviderId = keyof typeof PROVIDER_ICONS;
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, header);
console.log(`[provider-icons] wrote ${Object.keys(providers).length} providers → ${outFile}`);
